if (argv[0] === '--policy') {
  // Lint content against each file's DECLARED zone/intent (from .true-up.json zones).
  const report = argv.includes('--report')
  const zones = loadZones()
  const files = listFiles()
  const v = []
  for (const path of files) {
    const z = zoneOf(path, zones)
    if (!z) { v.push({ path, rule: 'unzoned', detail: 'no zone declared' }); continue }
    let c = null
    const content = () => { if (c === null) { try { c = readFileSync(resolve(ROOT, path), 'utf8') } catch { c = '' } } return c }
    if ((z.rules || []).includes('no-machine-local-paths')) {
      for (const h of machineLocalHits(content())) v.push({ path, zone: z.intent, rule: 'no-machine-local-paths', detail: `${h.match} (line ${h.line})` })
    }
    // no-private-operational-leak is enforced on ALL public files (listFiles excludes
    // private/), regardless of per-zone opt-in — a new public file/zone is auto-covered.
    if (z.visibility !== 'private') {
      for (const h of privateLeakHits(content())) v.push({ path, zone: z.intent, rule: 'no-private-operational-leak', detail: `${h.match} (line ${h.line})` })
    }
  }
  // must-be-ciphertext: scan the declaring zone's files for SOPS markers (private/ is excluded from listFiles)
  for (const z of zones) {
    if (!(z.rules || []).includes('must-be-ciphertext')) continue
    const pf = filesUnder(z.path)
    for (const f of pf) {
      let c = ''
      try { c = readFileSync(resolve(ROOT, f), 'utf8') } catch { continue }
      if (!isCiphertext(c)) v.push({ path: f, zone: z.intent, rule: 'must-be-ciphertext', detail: 'appears to be plaintext (no SOPS/age/pgp ciphertext structure — a bare "sops" mention does not count)' })
    }
  }
  // no-public->private-deps: historical rule name; enforce the full local visibility lattice.
  const g = build()
  failGraphBuildIfNeeded(g)
  const graphTaints = computeTaints(g)
  const addNonpublicImportViolation = (path, zone, detail) => {
    v.push({ path, zone: zone || '?', rule: 'no-public->nonpublic-import', detail })
  }
  for (const e of g.edges) {
    const fromPath = pathFromNodeId(e.from)
    const fz = fromPath ? zoneOf(fromPath, zones) : null
    const fromVis = visibilityForNode(g, e.from, zones)
    if (e.to.startsWith('import:')) {
      const imported = g.nodes[e.to] || {}
      const toAudience = effectiveImportedAudience(imported)
      if (audienceRank(fromVis) < audienceRank(toAudience)) {
        addNonpublicImportViolation(e.from, fz ? fz.intent : '?', `edge -> @${imported.importAlias}:${imported.importId} (${toAudience})`)
      }
      continue
    }
    const toVis = visibilityForNode(g, e.to, zones)
    if (audienceRank(fromVis) < audienceRank(toVis)) {
      v.push({ path: e.from, zone: fz ? fz.intent : '?', rule: 'no-public->private-deps', detail: `${fromVis} -> ${toVis} edge -> ${e.to}` })
    }
  }
  for (const [id, taints] of Object.entries(graphTaints)) {
    if (!id.startsWith('file:') || !taints.length) continue
    const n = g.nodes[id] || {}
    const path = id.slice(5)
    const z = zoneOf(path, zones)
    const vis = n.visibility || (z && z.visibility) || 'public'
    const maxTaint = maxAudienceLevel(taints.map((t) => t && t.audience))
    if (audienceRank(vis) < audienceRank(maxTaint)) {
      const t = taints.find((x) => audienceRank((x && x.audience) || 'public') === audienceRank(maxTaint)) || {}
      const who = t.alias || t.repoId || 'import'
      addNonpublicImportViolation(id, z ? z.intent : '?', `tainted by ${who}:${t.id || '?'} (${maxTaint})`)
    }
  }
  // report: zone coverage + violations + which advisory rules are declared-but-not-auto-enforced
  const byZone = {}
  for (const path of files) { const z = zoneOf(path, zones); const k = z ? z.intent : 'UNZONED'; byZone[k] = (byZone[k] || 0) + 1 }
  const MECH = ['no-machine-local-paths', 'must-be-ciphertext', 'no-public->private-deps', 'no-public->nonpublic-import', 'no-private-operational-leak']
  const advisory = new Set()
  for (const z of zones) for (const r of z.rules || []) if (!MECH.includes(r)) advisory.add(r)
  // no-private-operational-leak is enforced on EVERY public file regardless of zone opt-in — surface it
  // so a reader never mistakes "not in my zone's rules" for "not checked".
  const alwaysEnforced = ['no-private-operational-leak']
  if (JSONOUT) jout({ ok: v.length === 0, count: v.length, clean: v.length === 0, violations: v, zoneCoverage: byZone, enforcedRules: MECH, alwaysEnforced, advisoryRules: [...advisory] })
  else {
    console.log('zone coverage (intent: files):')
    for (const [k, n] of Object.entries(byZone).sort()) console.log(`  ${k}: ${n}`)
    console.log(`\npolicy violations: ${v.length}`)
    for (const x of v) console.log(`  [${x.rule}] ${x.path}${x.zone ? ' (' + x.zone + ')' : ''} — ${x.detail}`)
    console.log(`\nalways enforced on public files (not zone-opt-in): ${alwaysEnforced.join(', ')}`)
    if (advisory.size) console.log(`advisory rules (declared, not auto-enforced — manual/LLM review): ${[...advisory].join(', ')}`)
  }
  process.exit(report ? 0 : (v.length ? 1 : 0))
}

if (argv[0] === '--impact') {
  const since = flagVal('--since')
  const proofMode = argv.includes('--proof')
  if (since && !refExists(since)) jerr(`bad ${refLabel()}: ${since} (does not resolve). Not the same as "0 dependents".`, 2, { ref: since })
  if (proofMode && !since) jerr('--impact --proof requires --since <ref>', 2, { kind: 'usage', flag: '--proof' })
  let graph
  if (NOWRITE) graph = build() // --no-write: compute in memory, never read/write the cache (no "build first" dead-end)
  else try { graph = JSON.parse(readFileSync(OUT, 'utf8')) } catch { jerr(`build the graph first: ${rebuildHint}`, 2, { hint: 'run: true-up' }) }
  const targets = []
  for (let i = 1; i < argv.length; i++) {
    const a = argv[i]
    if (a === '--since') { i++; continue }
    if (a === '--proof') continue
    targets.push(a)
  }
  if (since && targets.length) jerr(`unexpected --impact target(s) with --since: ${targets.join(', ')}. Use either --impact --since <ref> OR --impact <path|path#fact>...`, 2, { kind: 'unexpected-arg', command: '--impact', got: targets })
  if (!since && !targets.length) jerr('usage: --impact <path|path#fact>...  OR  --impact --since <ref>', 2)
  const changedPaths = since ? vcsChanged(since) : []
  const { seeds, changedFacts, unknownTargets } = since ? seedsForChange(since, changedPaths, graph) : seedsFromTargets(targets, graph)
  // A typo'd target must be a usage ERROR, not a silent "0 dependents" (exit 0) — the same dangerous
  // false-negative the tool already fixed for a bad --since ref. Symmetry: unknown input → exit 2.
  if (unknownTargets && unknownTargets.length) jerr(`unknown --impact target(s): ${unknownTargets.join(', ')} (no matching file/fact node in the graph). Not the same as "0 dependents".`, 2, { unknownTargets })
  const allHits = dependentsOf(graph, seeds)
  // For a direct target, --impact is the full blast-radius query and includes symlink aliases. For
  // --since, the default view is "remaining stale"; a live symlink alias is already satisfied by the
  // filesystem, so it should not show up as pending mechanical work.
  const hits = since ? allHits.filter((h) => h.kind !== 'alias-of') : allHits
  const mech = hits.filter((h) => h.propagation === 'mechanical')
  const adv = hits.filter((h) => h.propagation === 'advisory')
  const proof = proofMode ? impactProof(graph, seeds, changedFacts, changedPaths) : null
  if (JSONOUT) jout({ ok: true, target: since ? { since } : { targets }, changedFacts, counts: { total: hits.length, mechanical: mech.length, advisory: adv.length }, mechanical: mech, advisory: adv, ...(proof ? { proof } : {}) })
  else {
    console.log(`change-impact of: ${since ? '--since ' + since : targets.join(', ')}`)
    if (changedFacts.length) console.log(`  facts changed: ${changedFacts.map((f) => f.split('#')[1]).join(', ')}`)
    console.log(`  ${hits.length} dependent(s) — ${mech.length} mechanical (regenerate/anchored), ${adv.length} advisory (review)\n`)
    if (mech.length) { console.log('  MECHANICAL:'); for (const h of mech) console.log(`    ${h.node}   [${h.kind}/${h.granularity}${h.via ? ' via ' + h.via : ''}]`) }
    if (adv.length) { console.log('\n  ADVISORY (owner reviews):'); for (const h of adv) console.log(`    ${h.node}   [${h.kind} <- ${h.fromSource}]`) }
    if (proof) {
      console.log(`\n  PROOF (changed sources/facts -> dependents; changed-in-range means edited in this diff, not semantically verified):`)
      for (const s of proof.sources) {
        console.log(`    ${s.source}${s.changedFact ? ' [changed fact]' : ''}`)
        for (const d of s.dependents) console.log(`      ${d.node}   [${d.status}]`)
      }
    }
  }
  process.exit(0)
}

if (argv[0] === '--verify-scope') {
  // The deterministic anti-code-golf gate. After a maintenance edit, EVERY changed file must be
  // "explained" by the dependency graph relative to <ref>: it is a changed source-of-truth, OR a
  // regenerated/advisory dependent of one, OR the graph cache. A changed file that is neither (an
  // out-of-blast-radius edit — e.g. an unanchored doc someone "tidied" because it mentions the
  // changed term) is scope creep / code-golf and FAILS the gate. Undeclared staleness is the
  // audit's job (it proposes an anchor), not maintenance's. Empirically: without this an LLM
  // expanded scope to such decoys 3/3 trials; with the worklist-subset discipline, 0/3.
  const since = flagVal('--since') || defaultSince()
  if (!refExists(since)) jerr(`bad ${refLabel()}: ${since}`, 2, { ref: since })
  const g = build()
  if (!g.edges.length) {
    if (JSONOUT) jout({ ok: true, vacuous: true, reason: 'no dependency edges declared — declare facts/seed in .true-up.json', changed: [], violations: [] })
    else console.error('NOTE: no dependency edges declared — scope check is vacuous (PASS ≠ GUARDED). Declare facts/seed in .true-up.json; run `true-up robot-docs` for the recipe.')
    process.exit(0)
  }
  const sources = new Set()
  for (const e of g.edges) { const s = nodePath(e.to); if (s) sources.add(s) }
  const changed = vcsChanged(since).filter(keepFile)
  // a changed file is a legitimate SEED only if something declares a dependency on it (it's a source) or it's a steward
  const changedSources = changed.filter((f) => sources.has(f) || FACTS[f])
  const { seeds } = seedsForChange(since, changedSources, g)
  for (const f of changedSources) if (g.nodes['file:' + f]) seeds.add('file:' + f)
  const explained = new Set(changedSources)
  for (const h of dependentsOf(g, seeds)) { const s = nodePath(h.node); if (s) explained.add(s) }
  const violations = changed.filter((f) => !explained.has(f))
  if (JSONOUT) { jout({ ok: violations.length === 0, since, changed, explained: [...explained], violations }); process.exit(violations.length ? 1 : 0) }
  if (violations.length) {
    console.error(`out-of-scope edit(s) since ${since} — changed but NOT explained by the dependency graph:`)
    for (const f of violations) console.error(`  ${f}  (no declared dependency links it to what changed — anchor it, or this is scope creep / code-golf)`)
    console.error('Maintenance stays inside the deterministic blast radius; undeclared staleness is the audit\'s job.')
    process.exit(1)
  }
  console.log(`scope OK: all ${changed.length} changed file(s) since ${since} are explained by the graph (no code-golf).`)
  process.exit(0)
}

if (argv[0] === 'run' || argv[0] === 'true-up') {
  // Deterministic truing-up loop. The slow LLM prose-rewrite stays OUT (that's the agentic
  // /workflow layer). Repo-agnostic: change set from git; generators derived from the graph's
  // edges (`via`), never a hardcoded list — so this runs in any repo with the marker/seed convention.
  let since = flagVal('--since')
  if (since) { if (!refExists(since)) jerr(`bad ${refLabel()}: ${since}`, 2, { ref: since }) }
  else { since = defaultSince() } // shallow/initial repo: degrade to the current revision
  const strict = argv.includes('--strict')
  let graph
  if (NOWRITE) graph = build()
  else try { graph = JSON.parse(readFileSync(OUT, 'utf8')) } catch { jerr(`build the graph first: ${rebuildHint}`, 2, { hint: 'run: true-up' }) }
  const changed = vcsChanged(since)
  const { seeds, changedFacts } = seedsForChange(since, changed, graph)
  const hits = dependentsOf(graph, seeds)
  const mech = hits.filter((h) => h.propagation === 'mechanical' && h.kind !== 'alias-of')
  const adv = hits.filter((h) => h.propagation === 'advisory')
  if (!JSONOUT) console.log(`true-up since ${since}: ${changed.length} changed path(s), ${changedFacts.length} changed fact(s) → ${mech.length} mechanical / ${adv.length} advisory dependents`)
  const gens = [...new Set(mech.map((h) => h.via).filter(Boolean))]
  // --no-write: DRY-RUN preview — never execute generators (they mutate content) or refresh the cache.
  if (NOWRITE) {
    if (JSONOUT) jout({ ok: true, since, dryRun: true, changed: changed.length, changedFacts: changedFacts.length, mechanical: mech.length, advisory: adv.length, wouldRegenerate: gens, advisoryWorklist: adv.map((h) => ({ doc: h.node.replace('file:', ''), from: h.fromSource.replace('fact:', ''), kind: h.kind })) })
    else {
      if (gens.length) console.log(`  (--no-write dry run) would regenerate via: ${gens.join(', ')}`)
      if (adv.length) { console.log(`\n  ADVISORY — ${adv.length} doc(s) need review (← which fact moved):`); for (const h of adv) console.log(`    ${h.node.replace('file:', '')}  ←  ${h.fromSource.replace('fact:', '')}`) } else { console.log('  advisory: none') }
      console.log('\n  (--no-write: nothing executed or written — dry-run preview)')
    }
    process.exit(0)
  }
  // 1. REGENERATE mechanical dependents — run the generators the edges name (data-driven)
  let regenFail = 0
  for (const gen of gens) {
    const genAbs = resolve(ROOT, gen)
    // Confine generators to the repo: `run` EXECUTES this path as code (it's named by an in-repo
    // marker/seed), so a `via` escaping the repo (../evil.mjs, /abs) would run arbitrary out-of-tree
    // code. Refuse it loudly instead of executing. (Generators are repo-supplied; trust ends at the repo.)
    if (relative(ROOT, genAbs).startsWith('..')) { console.error(`  ✗ refusing to run generator OUTSIDE the repo: ${gen}`); regenFail++; continue }
    // Capture the generator's stderr so a FAILED regen shows WHY (was swallowed by stdio:'ignore').
    // `via` is a repo-local generator path: JS uses node, common script extensions use their runner,
    // and extensionless/shebang tools execute directly.
    const inv = generatorInvocation(genAbs)
    try { execFileSync(inv.cmd, inv.args, { stdio: ['ignore', 'ignore', 'pipe'], cwd: ROOT, env: { ...process.env, TRUE_UP_REPO: ROOT } }); if (!JSONOUT) console.log(`  ✓ regenerated via ${gen}`) }
    catch (e) { const errTxt = (e && e.stderr ? e.stderr.toString().trim() : '') || (e && e.message) || 'unknown error'; console.error(`  ✗ regenerate FAILED: ${gen}\n      ${errTxt.split('\n').join('\n      ')}`); regenFail++ } // stderr = diagnostics (Axiom 4)
  }
  try { execFileSync('node', [self], { stdio: 'ignore', env: { ...process.env, TRUE_UP_REPO: ROOT } }) } catch {} // refresh the graph itself
  // 2. ADVISORY worklist — prose a human/LLM must rewrite; this CLI never edits prose
  if (!JSONOUT) {
    if (adv.length) {
      console.log(`\n  ADVISORY — ${adv.length} doc(s) need review (← which fact moved):`)
      for (const h of adv) console.log(`    ${h.node.replace('file:', '')}  ←  ${h.fromSource.replace('fact:', '')}`)
    } else { console.log('  advisory: none') }
  }
  // 3. VERIFY — repo-agnostic gate: policy clean (read exit code) + on-disk graph self-consistent
  let policyClean = true
  try { execFileSync('node', [self, '--policy'], { stdio: 'ignore', env: { ...process.env, TRUE_UP_REPO: ROOT } }) } catch { policyClean = false }
  let chk = false
  try { execFileSync('node', [self, '--check'], { stdio: 'ignore', env: { ...process.env, TRUE_UP_REPO: ROOT } }); chk = true } catch {}
  const green = regenFail === 0 && policyClean && chk
  if (!JSONOUT) {
    console.log(`\n  verify: regen ${regenFail === 0 ? 'ok' : 'FAILED'} · policy ${policyClean ? 'clean' : 'VIOLATIONS'} · depgraph ${chk ? 'in-sync' : 'STALE'} → ${green ? 'GREEN ✓' : 'NOT GREEN ✗'}`)
    if (green && adv.length) console.log('  (mechanical trued up; advisory prose review still pending — that is the /workflow layer)')
    if (gens.length) console.error('  note: run executed user-declared generators — preview without writing via: true-up run --no-write')
    // Failure verdict on STDERR too (parity with `gate`): a CI step that greps stderr must see the failure.
    if (!green) console.error(`run NOT GREEN — rerun the failing gate: ${rebuildHint} --check / ${rebuildHint} --policy / ${rebuildHint} --externalities`)
  }
  // --json (Axiom 8): structured result on stdout; glyphs/human lines suppressed above. Exit codes unchanged.
  if (JSONOUT) jout({ ok: green, since, changed: changed.length, changedFacts: changedFacts.length, mechanical: mech.length, advisory: adv.length, regenerated: gens, advisoryWorklist: adv.map((h) => ({ doc: h.node.replace(/^file:/, ''), from: h.fromSource.replace(/^fact:/, ''), kind: h.kind })), regen: { ran: gens, failed: regenFail }, verify: { regen: regenFail === 0, policyClean, depgraphInSync: chk }, green, advisoryPending: green && adv.length > 0 })
  if (!green) process.exit(1)
  if (strict && adv.length) process.exit(2)
  process.exit(0)
}

// Anything past here is build (no args, or the explicit `build` verb) or --check. Reject unknown
// commands instead of silently building + writing into the target repo (HIGH-4 foot-gun).
if (argv[0] && argv[0] !== '--check' && argv[0] !== 'build') {
  const guess = didYouMean(argv[0])
  const corrected = guess ? `${rebuildHint} ${guess} ${argv.slice(1).join(' ')}`.trim() : null
  jerr(`unknown command: ${argv[0]}${guess ? `\ndid you mean: ${guess} ?` : ''}\nrun: ${rebuildHint} --help   (or ${rebuildHint} capabilities for the full machine-readable contract)`, 2, { kind: 'unknown-command', given: argv[0], didYouMean: guess || null, corrected })
}

const graph = build()
failGraphBuildIfNeeded(graph)

if (argv[0] === '--check') {
  const fresh = serialize(graph)
  const relOut = rel(OUT)
  if (argv.includes('--committed')) {
    // The drift gate: does the VCS-stored graph blob match a fresh rebuild?
    // Git: staged blob (pre-commit) preferred, else HEAD (CI). jj-only: @ is the working-copy
    // commit, so the gate reads the graph as tracked in @ after jj snapshots the worktree.
    const committed = vcsCommittedBlob(relOut)
    const upToDate = committed !== null && committed === fresh
    const reason = committed === null ? (VCS_KIND === 'jj' ? 'not tracked in @' : 'not committed/staged') : (upToDate ? 'in sync' : 'stale vs fresh rebuild')
    if (JSONOUT) { jout({ ok: upToDate, mode: 'committed', vcs: VCS_KIND, path: relOut, upToDate, reason }); process.exit(upToDate ? 0 : 1) }
    if (committed === null) { console.error(`${relOut} is not ${VCS_KIND === 'jj' ? 'tracked in @' : 'committed/staged'} — build it and commit/track it (the VCS is the database).`); process.exit(1) }
    if (!upToDate) { console.error(`${relOut} (committed) is STALE vs a fresh rebuild. Regenerate and ${VCS_KIND === 'jj' ? 'track it in @' : 'stage it'}: ${rebuildHint}  (see what's affected: ${rebuildHint} --impact --since ${VCS_KIND === 'jj' ? '@-' : 'HEAD'})`); process.exit(1) }
    console.log(`${relOut} (committed) is up to date ✓`)
    process.exit(0)
  }
  // default: working-tree freshness — is the on-disk graph what a fresh build produces?
  let cur = ''
  let built = true
  try { cur = readFileSync(OUT, 'utf8') } catch { built = false } // distinguish "never built" from "stale"
  const upToDate = built && cur === fresh
  // "not built" is a setup state, not drift — say so, so an agent knows to BUILD, not debug a diff.
  const reason = !built ? 'not built' : (upToDate ? 'in sync' : 'stale on disk')
  if (JSONOUT) { jout({ ok: upToDate, mode: 'worktree', path: relOut, upToDate, built, reason }); process.exit(upToDate ? 0 : 1) }
  if (!built) { console.error(`${relOut} has not been built yet — run: ${rebuildHint}`); process.exit(1) }
  if (!upToDate) { console.error(`${relOut} is STALE (on disk). Regenerate: ${rebuildHint}  (see what's affected: ${rebuildHint} --impact --since ${VCS_KIND === 'jj' ? '@-' : 'HEAD'})`); process.exit(1) }
  console.log(`${relOut} is up to date ✓`)
  process.exit(0)
}

// --no-write: fully-stateless — compute the graph in memory and persist NOTHING (not even .true-up/).
// The fail-loud gates above still ran; --json emits the in-memory graph so CI/agents get the data
// without a disk artifact. This makes the write-invariant's strongest form ("writes literally nothing")
// available on demand.
if (!NOWRITE) {
  mkdirSync(dirname(OUT), { recursive: true })
  writeFileAtomic(OUT, serialize(graph))
}
const summary = graphSummary(graph)
// INERT = the drift-detection layer does nothing because there are no edges to propagate. Span
// facts + anchors count: a repo with code spans + <!-- fact: --> anchors has real edges and is NOT
// inert even with an empty config `facts` (the dogfood bug was keying inert off config.facts alone).
const inert = summary.inert
// We warn on a repo that has no edges a USER wired (declared/anchored/generator), NOT merely on 0
// edges: an incidental auto-detected symlink would otherwise suppress this onboarding NOTICE. The
// recipe pointer is the in-tool `true-up robot-docs` (always present), NOT docs/CONFIG.md — that file
// ships in true-up's own repo, not in the adopter's, so it was a dead breadcrumb. `summary.declaredEdges`
// (and `summary.tracking`) are also in the --json output so a build/graph consumer sees the same signal.
const trackingEdges = summary.declaredEdges
if (JSONOUT) jout({ ok: true, out: NOWRITE ? null : rel(OUT), wrote: !NOWRITE, ...summary, ...(NOWRITE ? { graph: { nodes: graph.nodes, edges: graph.edges } } : {}) })
else {
  console.log(`${rel(OUT)} ${NOWRITE ? 'computed (--no-write: NOT written)' : 'written'}: ${summary.nodes} nodes (${summary.factNodes} fact-nodes), ${summary.edges} edges`)
  console.log(`  edges by direction-basis: ${JSON.stringify(summary.byDirectionBasis)}`)
  // No config yet: bare `true-up` built with defaults and persisted a (gitignored) cache — say so and
  // point to init, so a first exploratory run isn't a silent write with no onboarding signal.
  if (!NOWRITE && !existsSync(resolve(ROOT, '.true-up.json'))) console.error(`  (no .true-up.json — built with defaults; run \`${rebuildHint} init\` to scaffold a config you can wire edges into)`)
  // Empty-graph notice: distinguish "nothing changed" from "you declared nothing to track" (report LIMITATION).
  if (!trackingEdges) console.error(`\nNOTICE: no dependency edges that track a source-of-truth — the drift-detection layer is INERT (it passes --check trivially). Give it something to track: add a "seed" edge to .true-up.json, an inline <!-- fact: path#key --> anchor, or bracket a code region (ANY language) with true-up:anchor / true-up:end. Full recipe: run \`${rebuildHint} robot-docs\`.`)
}
