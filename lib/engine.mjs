if (JSONOUT) jout({ out: NOWRITE ? null : rel(OUT), wrote: !NOWRITE, nodes: nNodes, factNodes: nFacts, edges: graph.edges.length, byDirectionBasis: byBasis, inert, ...(NOWRITE ? { graph: { nodes: graph.nodes, edges: graph.edges } } : {}) })
