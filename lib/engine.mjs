  console.log(`  edges by direction-basis: ${JSON.stringify(byBasis)}`)
  // Empty-graph notice: distinguish "nothing changed" from "you declared nothing to track" (report LIMITATION).
  if (inert) console.error(`\nNOTICE: no dependency edges — the drift-detection layer is INERT (it passes --check trivially). Give it something to track: declare steward facts/seed in .true-up.json, add an inline <!-- fact: path#key --> anchor, or bracket a code region (ANY language) with true-up:anchor / true-up:end. See docs/CONFIG.md.`)
