if (argv[0] === '--check') {
  const fresh = serialize(graph)
  const relOut = rel(OUT)
  if (argv.includes('--committed')) {
    // The drift gate: does the COMMITTED (or staged) graph blob match a fresh rebuild?
    // Catches "committed a source change without re-staging the regenerated graph", and an
    // untracked graph (false assurance). Prefers the staged blob (pre-commit), else HEAD (CI).
    const blob = (sp) => { try { return execFileSync('git', ['-C', ROOT, 'show', sp], { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }) } catch { return null } }
    const committed = blob(':' + relOut) ?? blob('HEAD:' + relOut) // string | null (blob never returns undefined)
    const upToDate = committed !== null && committed === fresh
    const reason = committed === null ? 'not committed/staged' : (upToDate ? 'in sync' : 'stale vs fresh rebuild')
    if (JSONOUT) { jout({ mode: 'committed', path: relOut, upToDate, reason }); process.exit(upToDate ? 0 : 1) }
    if (committed === null) { console.error(`${relOut} is not committed/staged — build it and commit it (git is the database).`); process.exit(1) }
    if (!upToDate) { console.error(`${relOut} (committed) is STALE vs a fresh rebuild. Regenerate and stage: ${rebuildHint}`); process.exit(1) }
    console.log(`${relOut} (committed) is up to date ✓`)
    process.exit(0)
  }
  // default: working-tree freshness — is the on-disk graph what a fresh build produces?
  let cur = ''
  try { cur = readFileSync(OUT, 'utf8') } catch {}
  const upToDate = cur === fresh
  if (JSONOUT) { jout({ mode: 'worktree', path: relOut, upToDate, reason: upToDate ? 'in sync' : 'stale on disk' }); process.exit(upToDate ? 0 : 1) }
  if (!upToDate) { console.error(`${relOut} is STALE (on disk). Regenerate: ${rebuildHint}`); process.exit(1) }
  console.log(`${relOut} is up to date ✓`)
  process.exit(0)
}

