#!/usr/bin/env node
// true-up — build (or --check, or query --impact) the typed dependency graph of a repo.
//
//   true-up                                  # (re)write the graph JSON (path from .true-up.json `out`)
//   true-up --check                          # exit 1 if the ON-DISK graph is stale (working-tree freshness)
//   true-up --check --committed              # exit 1 if the COMMITTED/STAGED graph blob is stale (the drift gate)
//   true-up --impact <path|path#fact>...     # who is made stale (add --since <ref> for a change range)
//   true-up --policy        [--report]       # zone/visibility lint; EXIT 1 on violations (--report = exit 0)
//   true-up --externalities [--report]       # machine-local-path leak scan; EXIT 1 on leaks (--report = exit 0)
//   true-up run [--since <ref>] [--strict]   # deterministic truing-up loop (regenerate + verify); exit 1 if not GREEN
//   true-up init                             # scaffold a starter .true-up.json
//   true-up --help                           # this help (writes nothing)
//
// All paths/targets are relative to the TARGET repo: --repo <path> | $TRUE_UP_REPO | git toplevel of CWD | CWD.
//
// LAYER 2 (detection). A typed, content-hashed, *directed* dependency graph spanning
// code + data + prose, whose edges carry a CAUSAL direction (from = dependent /
// downstream, to = source-of-truth).
//
// Direction never comes from correlation. Only from a causal basis:
//   - generator : a build script's input->output, from the self-describing
//                 "generated by X from Y" markers already in the repo.    (mechanical)
//   - declared  : a steward registry in .true-up.json `seed`.            (advisory)
//   - anchored  : an inline marker in the dependent pointing at a FACT
//                 (<!-- fact: <path>#<arrayProp>.<key> -->).             (mechanical, fact-granular)
// Correlation signals (co-change / embeddings) may only PROPOSE undirected
// candidates; they never assign the arrow, so none are emitted here.
//
// Granularity: steward data files are decomposed into per-fact nodes (content-hashed
// individually) so an anchored dependent is only stale when THAT fact's hash moves
// (early-cutoff) — not when any unrelated part of the file changes.
//
// Fail-loud: an anchor whose fact path does not resolve is a hard error (aplumb's
// "stable IDs, fail on mismatch" lesson) — a silently-wrong anchor is worse than none.
//
// Zero-dep node, deterministic output (sorted, no timestamps) so --check is honest.

import { readFileSync, writeFileSync, mkdirSync, existsSync, lstatSync, readlinkSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve, relative } from 'node:path'
import { createHash } from 'node:crypto'
import { execFileSync } from 'node:child_process'

const self = fileURLToPath(import.meta.url)
const here = (p) => resolve(dirname(self), p)

// This is the TOOL: it operates on a TARGET repo, not its own directory.
// Target = --repo <path> | $TRUE_UP_REPO | git toplevel of CWD | CWD.
function resolveRoot() {
  const i = process.argv.indexOf('--repo')
  if (i >= 0 && process.argv[i + 1]) return resolve(process.argv[i + 1])
  if (process.env.TRUE_UP_REPO) return resolve(process.env.TRUE_UP_REPO)
  try { return execFileSync('git', ['rev-parse', '--show-toplevel'], { encoding: 'utf8' }).trim() } catch { return process.cwd() }
}
const ROOT = resolveRoot()

// Per-repo config: <ROOT>/.true-up.json (or true-up.config.json) declares stewards (facts),
// zones, declared edges, and the output path. Everything repo-specific lives here, not in code.
function loadConfig() {
  for (const name of ['.true-up.json', 'true-up.config.json']) {
    try { return JSON.parse(readFileSync(resolve(ROOT, name), 'utf8')) } catch {}
  }
  return {}
}
const CONFIG = loadConfig()
const OUT = resolve(ROOT, CONFIG.out || '.true-up/depgraph.json')
// steward files decomposed into per-fact nodes: path -> [ [arrayProp, keyField] ]  (from config)
const FACTS = CONFIG.facts || {}

const sha256 = (s) => createHash('sha256').update(s).digest('hex').slice(0, 16)
const rel = (abs) => relative(ROOT, abs)
// User-facing "rebuild" hint. When invoked cross-repo (--repo), the engine path relative to the
// TARGET ascends out of it ("../../…/bin"), which is confusing guidance — fall back to the command name.
const rebuildHint = relative(ROOT, self).startsWith('..') ? 'true-up' : relative(ROOT, self)

// deep key-sorted stringify so a fact's hash is order-independent
function sortKeys(v) {
  if (Array.isArray(v)) return v.map(sortKeys)
  if (v && typeof v === 'object') {
    const o = {}
    for (const k of Object.keys(v).sort()) o[k] = sortKeys(v[k])
    return o
  }
  return v
}
const stable = (v) => JSON.stringify(sortKeys(v))
// Blank out fenced + inline code so syntax shown in DOCUMENTATION (a doc explaining true-up's own
// <!-- fact: --> / generated-by markers, OR a privacy policy quoting a forbidden path shape) is never
// parsed as a live anchor/marker or flagged as a leak. Preserves line count (replaces non-newline
// chars with spaces) so line numbers stay accurate.
const stripCode = (s) => s.replace(/```[\s\S]*?```/g, (m) => m.replace(/[^\n]/g, ' ')).replace(/`[^`\n]+`/g, (m) => ' '.repeat(m.length))

// Per-line suppression: a legitimate path-example in prose (e.g. the privacy policy itself) can opt out.
//   <!-- true-up:ignore-line [rule] -->   suppresses findings on THIS line
//   <!-- true-up:ignore-next [rule] -->   suppresses findings on the NEXT line
// Optional [rule] (e.g. no-machine-local-paths) scopes it; omitted = all rules. Checked against the
// ORIGINAL line text (so the directive is seen even if stripCode would blank a code span around it).
const SUPPRESS_LINE = /<!--\s*true-up:ignore-line(?:\s+([\w-]+))?\s*-->/
const SUPPRESS_NEXT = /<!--\s*true-up:ignore-next(?:\s+([\w-]+))?\s*-->/
function suppressor(origLines) {
  return (i, rule) => {
    const ml = (origLines[i] || '').match(SUPPRESS_LINE)
    if (ml && (!ml[1] || ml[1] === rule)) return true
    const mn = (i > 0 ? origLines[i - 1] || '' : '').match(SUPPRESS_NEXT)
    if (mn && (!mn[1] || mn[1] === rule)) return true
    return false
  }
}

// ---- zones / policy ----
function loadZones() {
  // default: one public-default zone so --policy enforces no-machine-local-paths everywhere out of the box
  return CONFIG.zones || [{ path: '', visibility: 'public', audience: 'world', intent: 'public-default', rules: ['no-machine-local-paths'] }]
}
function zoneScore(zpath, path) {
  if (zpath === path) return 1000
  if (zpath.startsWith('**/') && path.endsWith(zpath.slice(2))) return 600 + zpath.length
  if (zpath.startsWith('*') && path.endsWith(zpath.slice(1))) return 500 + zpath.length
  if (zpath.endsWith('/') && path.startsWith(zpath)) return 100 + zpath.length
  if (zpath === '' || zpath === '/') return 1
  return -1
}
function zoneOf(path, zones) {
  let best = null
  let bestS = 0
  for (const z of zones) { const s = zoneScore(z.path, path); if (s > bestS) { bestS = s; best = z } }
  return best
}
// machine-local path leaks (canonical ~/.claude etc. allowlisted). Scans stripCode'd content
// (doc code-spans are illustrative, not leaks) and honors per-line suppression directives.
const EXT_PATS = [
  { re: /\/home\/[a-z][\w.\-]*\//gi, sev: 'high' },
  { re: /\/Users\/[A-Za-z][\w.\-]*\//g, sev: 'high' },
  { re: /(?<![$\w])~\/[\w./\-]+/g, sev: 'med' },
]
const EXT_ALLOW = [/^~\/\.(claude|config|cache)\b/]
function machineLocalHits(content) {
  const orig = content.split('\n')
  const scan = stripCode(content).split('\n')
  const sup = suppressor(orig)
  const hits = []
  scan.forEach((line, i) => {
    if (sup(i, 'no-machine-local-paths')) return
    for (const p of EXT_PATS) {
      p.re.lastIndex = 0
      let m
      while ((m = p.re.exec(line))) { if (EXT_ALLOW.some((a) => a.test(m[0]))) continue; hits.push({ line: i + 1, sev: p.sev, match: m[0] }) }
    }
  })
  return hits
}

// Private OPERATIONAL/AUTH mechanics that must never appear in a PUBLIC file. This is
// the `share-method-not-operator` advisory PROMOTED to a mechanical rule for the
// concrete tokens that have leaked or are unambiguously operator/auth detail. Naming a
// provider as a sanitized conclusion (a "subscription bridge", a model slug) is allowed
// and intentionally NOT matched; its AUTH MECHANICS — credential file paths, private
// adapter class names, raw backend endpoints — are what leak. Add a token the moment a
// new class of operational detail is found in a public file (harness rule: a leak that
// happened once becomes a lint that can't happen again).
//
// The banned literals are ASSEMBLED FROM FRAGMENTS so this definition never contains a
// contiguous banned token — the rule polices itself without self-tripping (same spirit
// as the escaped machine-local regexes above). Reword comments/docs, never inline a
// literal, when extending this list.
const _leak = (s) => new RegExp(s, 'i')
const PRIVATE_LEAK_PATS = [
  _leak('auth' + '\\.' + 'json'),                        // a credential JSON file
  _leak('\\.cod' + 'ex/auth|\\.pi/' + 'agent'),          // agent-CLI credential directories
  _leak('back' + 'end-api|chatgpt\\.com/back' + 'end'),  // raw subscription backend endpoint
  new RegExp('Codex' + 'SubscriptionLM'),                // a private adapter class name
]
function privateLeakHits(content) {
  const orig = content.split('\n')
  const scan = stripCode(content).split('\n')
  const sup = suppressor(orig)
  const hits = []
  scan.forEach((line, i) => {
    if (sup(i, 'no-private-operational-leak')) return
    for (const re of PRIVATE_LEAK_PATS) {
      const m = re.exec(line)
      if (m) hits.push({ line: i + 1, match: m[0] })
    }
  })
  return hits
}

// per-fact hashes for a steward file -> { factId: hash }
function extractFacts(path, content) {
  const out = {}
  const spec = FACTS[path]
  if (!spec) return out
  let data
  try { data = JSON.parse(content) } catch { return out }
  for (const [arrProp, keyField] of spec) {
    const arr = data[arrProp]
    if (!Array.isArray(arr)) continue
    for (const item of arr) {
      const key = item && item[keyField]
      if (key === null || key === undefined) continue
      out[`fact:${path}#${arrProp}.${key}`] = sha256(stable(item))
    }
  }
  return out
}

const gitLines = (args) => {
  try { return execFileSync('git', ['-C', ROOT, ...args], { encoding: 'utf8' }).split('\n').filter(Boolean) }
  catch { return [] }
}
function listFiles() {
  const all = [...new Set([...gitLines(['ls-files']), ...gitLines(['ls-files', '--others', '--exclude-standard'])])]
  const KEEP = /\.(md|json|mjs|js)$/
  const SKIP = /^(private\/|node_modules\/|\.git\/|\.jj\/|\.true-up\/)|^\.true-up\.json$|^true-up\.config\.json$|depgraph\.(json|seed\.json)$/
  return all.filter((p) => KEEP.test(p) && !SKIP.test(p)).sort()
}

const extKind = (p) => /\.(mjs|js|py|ts|tsx|rb|go|rs|sh|c|cc|cpp|h|java|kt|swift)$/.test(p) ? 'code' : p.endsWith('.json') ? 'data' : 'prose'

function classify(path, content) {
  if (/\.(mjs|js)$/.test(path)) return { kind: 'code', role: 'none' }
  if (path.endsWith('.json')) {
    if (FACTS[path]) return { kind: 'data', role: 'steward' }
    return { kind: 'data', role: 'none' }
  }
  if (/^<!--\s*generated by /m.test(content.slice(0, 400))) return { kind: 'generated', role: 'guard' }
  if (path.endsWith('SKILL.md')) return { kind: 'prose', role: 'writer', audience: 'external-agents' }
  if (path === 'README.md' || /\/README\.md$/.test(path)) return { kind: 'prose', role: 'writer', audience: 'users' }
  if (path === 'AGENTS.md') return { kind: 'prose', role: 'writer', audience: 'maintainers' }
  return { kind: 'prose', role: 'writer', audience: 'mixed' }
}

// Resolve a generator's input file from its `here('./X.json')` literal, relative to the
// generator's own directory (not a hardcoded prefix), so generators live anywhere.
function generatorSource(genRel) {
  try {
    const m = readFileSync(resolve(ROOT, genRel), 'utf8').match(/here\(['"]\.\/([\w.\-]+\.json)['"]\)/)
    if (m) return rel(resolve(ROOT, dirname(genRel), m[1]))
  } catch {}
  return null
}

function mkEdge(from, to, kind, directionBasis, propagation, granularity, via) {
  return { from, to, kind, directionBasis, propagation, granularity, ...(via ? { via } : {}) }
}

function build() {
  const files = listFiles()
  const nodes = {}
  const edges = []
  const contents = {}
  const anchorErrors = []
  const symlinks = []

  for (const path of files) {
    const abs = resolve(ROOT, path)
    let st
    try { st = lstatSync(abs) } catch { continue }
    if (st.isSymbolicLink()) {
      // a symlink is an ALIAS of its target (e.g. CLAUDE.md -> AGENTS.md): same bytes,
      // one canonical source. Do NOT read the followed content as an independent node.
      let targetRel = null
      try { targetRel = rel(resolve(dirname(abs), readlinkSync(abs))) } catch {}
      nodes['file:' + path] = { kind: 'symlink', role: 'alias', ...(targetRel ? { symlinkTo: targetRel } : {}), hash: sha256('symlink->' + (targetRel || '?')) }
      if (targetRel) symlinks.push({ from: path, to: targetRel })
      continue
    }
    let content
    try { content = readFileSync(abs, 'utf8') } catch { continue }
    contents[path] = content
    const m = classify(path, content)
    nodes['file:' + path] = { kind: m.kind, role: m.role, ...(m.audience ? { audience: m.audience } : {}), hash: sha256(content) }
    // decompose steward data files into per-fact nodes
    for (const [factId, h] of Object.entries(extractFacts(path, content))) {
      nodes[factId] = { kind: 'fact', role: 'steward-fact', hash: h }
    }
  }

  for (const path of files) {
    const content = contents[path]
    if (!content) continue
    if (/\.(mjs|js)$/.test(path)) continue // code files carry marker strings as literals
    const scan = stripCode(content) // markers/anchors inside code formatting are docs, not live
    // whole-file generated marker
    const whole = scan.match(/<!--\s*generated by\s+([^\s)]+)(?:\s+from\s+([^\s)]+))?/)
    if (whole && /^<!--\s*generated by /m.test(scan.slice(0, 400))) {
      const source = whole[2] || generatorSource(whole[1])
      if (source && nodes['file:' + source]) edges.push(mkEdge('file:' + path, 'file:' + source, 'generated-from', 'generator', 'mechanical', 'file', whole[1]))
    }
    // generated BLOCK marker
    const blk = scan.match(/<!--\s*([A-Z]+):START\s*\(generated by\s+([^\s)]+)[^]*?-->([^]*?)<!--\s*\1:END\s*-->/)
    if (blk) {
      const [, tag, gen, body] = blk
      const source = generatorSource(gen)
      nodes[`block:${path}#${tag}`] = { kind: 'generated', role: 'guard', hash: sha256(body) }
      if (source && nodes['file:' + source]) edges.push(mkEdge(`block:${path}#${tag}`, 'file:' + source, 'generated-from', 'generator', 'mechanical', 'span', gen))
    }
    // anchored fact markers -> directed, fact-granular, mechanical (FAIL LOUD if unresolved)
    for (const m of scan.matchAll(/<!--\s*fact:\s*([\w./\-]+#[\w.\-]+)\s*-->/g)) {
      const factId = 'fact:' + m[1]
      // anchored prose dep: fact-GRANULAR (fires only on that fact) but ADVISORY (a human/LLM
      // rewrites the prose) — only generated-from is mechanical/regenerable.
      if (nodes[factId]) edges.push(mkEdge('file:' + path, factId, 'derives-facts-from', 'anchored', 'advisory', 'fact', null))
      else anchorErrors.push({ file: path, anchor: m[1] })
    }
  }

  // declared edges (file granularity, advisory) from the steward registry. A declared edge may
  // reference a tracked source file that is NOT a graph node by default (e.g. a .py registry); add a
  // minimal node for such an endpoint so the coarse edge is not silently dropped (report LIMITATION).
  const tracked = new Set(gitLines(['ls-files']))
  for (const e of CONFIG.seed || []) {
    for (const ep of [e.from, e.to]) {
      const id = 'file:' + ep
      if (!nodes[id] && tracked.has(ep)) {
        let c = ''
        try { c = readFileSync(resolve(ROOT, ep), 'utf8') } catch {}
        nodes[id] = { kind: extKind(ep), role: 'none', hash: sha256(c) }
      }
    }
    if (nodes['file:' + e.from] && nodes['file:' + e.to]) {
      edges.push(mkEdge('file:' + e.from, 'file:' + e.to, e.kind || 'derives-facts-from', 'declared', 'advisory', 'file', null))
    }
  }

  // symlink alias edges (causal basis: symlink — the alias IS the canonical file's content)
  for (const s of symlinks) {
    if (nodes['file:' + s.to]) edges.push(mkEdge('file:' + s.from, 'file:' + s.to, 'alias-of', 'symlink', 'mechanical', 'file', null))
  }

  // tag each file/symlink node with its declared zone + intent (the graph DOCUMENTS the intention)
  const zones = loadZones()
  for (const id of Object.keys(nodes)) {
    if (!id.startsWith('file:')) continue
    const z = zoneOf(id.slice(5), zones)
    if (z) { nodes[id].zone = z.intent; nodes[id].visibility = z.visibility; if (z.audience) nodes[id].audience = z.audience }
  }

  const sortedNodes = {}
  for (const k of Object.keys(nodes).sort()) sortedNodes[k] = nodes[k]
  edges.sort((a, b) => (a.from + a.to + a.kind).localeCompare(b.from + b.to + b.kind))
  return { nodes: sortedNodes, edges, anchorErrors }
}

function serialize(graph) {
  return JSON.stringify(
    { _comment: 'GENERATED by true-up (do not hand-edit). Edit sources/markers/.true-up.json seed and regenerate. Edge arrow: from=dependent -> to=source-of-truth.', nodes: graph.nodes, edges: graph.edges },
    null, 2,
  ) + '\n'
}

// reverse reachability: all dependents of the seed node-set
function dependentsOf(graph, seeds) {
  const rev = new Map()
  for (const e of graph.edges) {
    if (!rev.has(e.to)) rev.set(e.to, [])
    rev.get(e.to).push(e)
  }
  const out = []
  const seen = new Set(seeds)
  let frontier = [...seeds]
  while (frontier.length) {
    const next = []
    for (const src of frontier) for (const e of rev.get(src) || []) {
      if (seen.has(e.from)) continue
      seen.add(e.from)
      out.push({ node: e.from, kind: e.kind, propagation: e.propagation, granularity: e.granularity, via: e.via || null, fromSource: src })
      next.push(e.from)
    }
    frontier = next
  }
  return out
}

