function graphSummary(graph) {
  // declaredEdges = edges a USER/extractor wired (declared seed / anchored / generator), EXCLUDING
  // auto-detected `alias-of` symlink edges. `tracking` (declaredEdges>0) is the honest "is the drift
  // layer doing anything for the user" signal; `inert` stays the documented raw-edge-count flag.
  const declaredEdges = graph.edges.filter((e) => e.directionBasis !== 'symlink').length
  return {
    nodes: Object.keys(graph.nodes).length,
    factNodes: Object.keys(graph.nodes).filter((k) => k.startsWith('fact:')).length,
    importedFactNodes: Object.keys(graph.nodes).filter((k) => k.startsWith('import:')).length,
    edges: graph.edges.length,
    declaredEdges,
    byDirectionBasis: graph.edges.reduce((a, e) => ((a[e.directionBasis] = (a[e.directionBasis] || 0) + 1), a), {}),
    inert: !graph.edges.length,
    tracking: declaredEdges > 0,
  }
}
