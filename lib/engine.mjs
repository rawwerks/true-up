// Seeds for explicit targets (paths or path#fact); steward facts compared working-tree vs the committed graph.
function seedsFromTargets(targets, graph) {
  const seeds = new Set()
  const changedFacts = []
  for (const t of targets) {
    if (t.includes('#')) {
      const id = 'fact:' + t
      if (graph.nodes[id]) seeds.add(id)
      else console.error(`(warning: fact node not found: ${id})`)
      continue
    }
    if (graph.nodes['file:' + t]) seeds.add('file:' + t)
    for (const id of Object.keys(graph.nodes)) if (id.startsWith(`block:${t}#`)) seeds.add(id)
    if (FACTS[t]) {
      let cur = ''
      try { cur = readFileSync(resolve(ROOT, t), 'utf8') } catch {}
      for (const [fid, h] of Object.entries(extractFacts(t, cur))) if (graph.nodes[fid] && graph.nodes[fid].hash !== h) { seeds.add(fid); changedFacts.push(fid) }
    }
  }
  return { seeds, changedFacts }
}
// Seeds for everything changed since <ref>; steward facts compared at <ref> vs now (early-cutoff).
function seedsForChange(ref, paths, graph) {
  const seeds = new Set()
  const changedFacts = []
  for (const t of paths) {
    if (graph.nodes['file:' + t]) seeds.add('file:' + t)
    for (const id of Object.keys(graph.nodes)) if (id.startsWith(`block:${t}#`)) seeds.add(id)
    if (FACTS[t]) {
      let cur = ''
      try { cur = readFileSync(resolve(ROOT, t), 'utf8') } catch {}
      const now = extractFacts(t, cur)
      const old = factsAt(ref, t)
      for (const fid of new Set([...Object.keys(now), ...Object.keys(old)])) if (now[fid] !== old[fid]) { if (graph.nodes[fid]) seeds.add(fid); changedFacts.push(fid) }
    }
    // span facts (Tier 1, any language): compare this file's span-fact hashes now vs at <ref>, so a
    // doc anchored to a code span is stale only when THAT span's bytes move (early-cutoff).
    let curSpan = ''
    try { curSpan = readFileSync(resolve(ROOT, t), 'utf8') } catch {}
    if (curSpan.includes('true-up:anchor') || Object.keys(graph.nodes).some((k) => k.startsWith(`fact:${t}#`))) {
      const nowS = extractSpans(t, curSpan).facts
      const oldS = spansAt(ref, t)
      for (const fid of new Set([...Object.keys(nowS), ...Object.keys(oldS)])) if (nowS[fid] !== oldS[fid]) { if (graph.nodes[fid]) seeds.add(fid); changedFacts.push(fid) }
    }
    // symbol facts (Tier 2): compare tree-sitter symbol hashes now vs at <ref> (symbol-granular early-cutoff)
    if (SYMBOLS && symbolExts().some((e) => t.endsWith(e))) {
      const nowY = extractSymbols(t, curSpan)
      const oldY = extractSymbols(t, gitShow(ref, t))
      for (const fid of new Set([...Object.keys(nowY), ...Object.keys(oldY)])) if (nowY[fid] !== oldY[fid]) { if (graph.nodes[fid]) seeds.add(fid); changedFacts.push(fid) }
    }
  }
  return { seeds, changedFacts }
}

