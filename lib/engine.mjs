// Last-resort guard: any uncaught throw (an ill-typed config reaching deep code, an unexpected fs error)
// exits CLEAN (code 2) with a one-line message — never a raw stack trace. A stack trace dumps the
// engine's own absolute install path (a machine-local path — ironic for a leak detector) and gives an
// agent no machine-readable signal. Specific call sites still fail loud with better messages; this is
// the floor, not the ceiling.
process.on('uncaughtException', (e) => { try { process.stderr.write(`true-up: ${(e && e.message) || e}\n`) } catch {} process.exit(2) })

// This is the TOOL: it operates on a TARGET repo, not its own directory.
// Target = --repo <path> | $TRUE_UP_REPO | CWD — and in EVERY case we normalize to the VCS TOPLEVEL.
// Why normalize (not take the path verbatim): a --repo pointing at a SUBDIR of a repo would otherwise
// miss the toplevel .true-up.json and see an empty file universe → nodes:0, edges:0, exit 0 — a
// FALSE-CLEAN that silently defeats the tool's own leak/policy gates (the headline cross-repo flag
// defeating the headline guarantee). A nonexistent or non-VCS path is reported (ROOT_IS_REPO=false →
// clean exit 2 at dispatch) instead of scanned as empty. VCS stderr is suppressed so no stray `fatal:`
// leaks to a caller — and so `--version`/`--help`/`capabilities` are clean outside any repo.
function probeVcsRoot(cand) {
  let top = null
  try { top = execFileSync('git', ['-C', cand, 'rev-parse', '--show-toplevel'], { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim() } catch {}
  if (top) return { root: top, kind: 'git' }
  try { top = execFileSync('jj', ['-R', cand, '--no-pager', 'root'], { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim() } catch {}
  if (top) return { root: top, kind: 'jj' }
  // `jj -R <nested-subdir> root` is not reliable for non-colocated workspaces on all jj versions,
  // while `jj root` from that CWD is. Agents often run tools from deep subdirs, so try the cwd form
  // for existing directories only. Do not dirname-fallback a file or nonexistent explicit --repo path:
  // that would silently retarget the parent jj workspace instead of failing closed.
  try {
    if (existsSync(cand) && lstatSync(cand).isDirectory()) {
      top = execFileSync('jj', ['--no-pager', 'root'], { cwd: cand, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim()
    }
  } catch {}
  if (top) return { root: top, kind: 'jj' }
  return null
}
function resolveRoot() {
  const i = process.argv.indexOf('--repo')
  let source = 'cwd'
  let explicit = null
  if (i >= 0 && process.argv[i + 1]) {
    source = '--repo'
    explicit = resolve(process.argv[i + 1])
  } else if (process.env.TRUE_UP_REPO) {
    source = '$TRUE_UP_REPO'
    explicit = resolve(process.env.TRUE_UP_REPO)
  }
  const cand = explicit || process.cwd()
  const probed = probeVcsRoot(cand)
  if (probed) return { root: probed.root, isRepo: true, kind: probed.kind, explicit, requested: cand, source, exists: existsSync(cand) }
  return { root: cand, isRepo: false, kind: null, explicit, requested: cand, source, exists: existsSync(cand) }
}
