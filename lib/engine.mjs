// reverse reachability: all dependents of the seed node-set
function dependentsOf(graph, seeds) {
  const rev = new Map()
  for (const e of graph.edges) {
    if (!rev.has(e.to)) rev.set(e.to, [])
    rev.get(e.to).push(e)
  }
  const out = []
  const seen = new Set(seeds)
  let frontier = [...seeds]
  while (frontier.length) {
    const next = []
    for (const src of frontier) for (const e of rev.get(src) || []) {
      if (seen.has(e.from)) continue
      seen.add(e.from)
      out.push({ node: e.from, kind: e.kind, propagation: e.propagation, granularity: e.granularity, via: e.via || null, fromSource: src })
      next.push(e.from)
    }
    frontier = next
  }
  return out
}

function nodePath(id) {
  if (id.startsWith('file:')) return id.slice(5)
  if (id.startsWith('fact:')) return id.slice(5).split('#')[0]
  if (id.startsWith('block:')) return id.slice(6).split('#')[0]
  return null
}

function impactProof(graph, seeds, changedFacts, changedPaths) {
  const changed = new Set(changedPaths)
  const changedFactSet = new Set(changedFacts)
  const sources = []
  for (const seed of [...seeds].sort()) {
    const dependents = dependentsOf(graph, new Set([seed])).map((h) => {
      const path = nodePath(h.node)
      const changedInRange = !!(path && changed.has(path))
      const satisfiedByAlias = h.kind === 'alias-of'
      const status = satisfiedByAlias ? 'satisfied-by-live-alias' : (changedInRange ? 'changed-in-range' : 'not-changed-in-range')
      return { ...h, path, changedInRange, satisfiedByAlias, status }
    })
    if (!dependents.length && !changedFactSet.has(seed)) continue
    sources.push({ source: seed, changedFact: changedFactSet.has(seed), dependents })
  }
  const all = sources.flatMap((s) => s.dependents)
  const unique = new Map()
  for (const d of all) if (!unique.has(d.node)) unique.set(d.node, d)
  const uniqueDependents = [...unique.values()]
  return {
    changed: [...changed].sort(),
    changedFacts,
    summary: {
      sources: sources.length,
      sourceDependentEdges: all.length,
      dependents: all.length,
      uniqueDependents: uniqueDependents.length,
      changedInRange: all.filter((d) => d.changedInRange).length,
      notChangedInRange: all.filter((d) => !d.changedInRange && !d.satisfiedByAlias).length,
      satisfiedByAlias: all.filter((d) => d.satisfiedByAlias).length,
      uniqueChangedInRange: uniqueDependents.filter((d) => d.changedInRange).length,
      uniqueNotChangedInRange: uniqueDependents.filter((d) => !d.changedInRange && !d.satisfiedByAlias).length,
      uniqueSatisfiedByAlias: uniqueDependents.filter((d) => d.satisfiedByAlias).length,
    },
    sources,
  }
}

