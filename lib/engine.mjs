// — graph built/stale, edge count, what's stale since <ref> (mechanical + advisory), policy/leak status,
// and copy-paste nextCommands. The single most canonical first guess for an agent ("status"/"triage"/
// "doctor" all route here). READ-ONLY; exits 0 as a probe and 2 for usage errors such as a bad
// --since ref/revset. It never writes (in-memory build, like --no-write).
if (argv[0] === 'status') {
  const graph = build() // in-memory; never persisted
  const fresh = serialize(graph)
  let onDisk = '', built = true
  try { onDisk = readFileSync(OUT, 'utf8') } catch { built = false }
  const inSync = built && onDisk === fresh
  const staleReason = !built ? 'not built' : (inSync ? 'in sync' : 'stale on disk')
  let since = flagVal('--since')
  if (since && !refExists(since)) jerr(`bad ${refLabel()}: ${since}`, 2, { ref: since })
  if (!since) since = defaultSince()
  const changedPaths = vcsChanged(since)
  const workspace = workspaceInfo(since)
  const cmdHint = commandHintFor(workspace)
  workspace.commandPrefix = cmdHint
  const sinceArg = shellQuote(since)
  const { seeds, changedFacts } = seedsForChange(since, changedPaths, graph)
  const allHits = dependentsOf(graph, seeds)
  // For a direct target, --impact is the full blast-radius query and includes symlink aliases. For
  // --since, the default view is "remaining stale"; a live symlink alias is already satisfied by the
  // filesystem, so it should not show up as pending mechanical work.
  const hits = since ? allHits.filter((h) => h.kind !== 'alias-of') : allHits
  // A live symlink alias is already "regenerated" by the filesystem. Keep alias-of edges in explicit
  // --impact blast-radius reports, but don't leave `status` telling agents to run an impossible regen loop.
  const mech = hits.filter((h) => h.propagation === 'mechanical' && h.kind !== 'alias-of')
  const adv = hits.filter((h) => h.propagation === 'advisory')
  // policy + externalities via the same child-process gates `gate` uses (reuse the exact logic; read exit code)
  const env = { ...process.env, TRUE_UP_REPO: ROOT }
  const childOk = (a) => { try { execFileSync('node', [self, ...a], { stdio: 'ignore', env }); return true } catch { return false } }
  const policyOk = childOk(['--policy'])
  const extOk = childOk(['--externalities'])
  // Only count what would ACTUALLY fail a build (fail-loud): unresolved anchors + bad seed endpoints,
  // plus span errors ONLY under strictSpans. A bare `true-up:anchor` token mentioned in docs/code is
  // lenient-by-default (not an error) — don't scare an agent with benign self-mentions.
  const buildErrs = graph.anchorErrors.length + graph.seedErrors.length + (graph.importErrors || []).length + (CONFIG.strictSpans ? (graph.spanErrors || []).length : 0)
  // TRACKING vs done. A graph with no user/extractor-wired edges is tracking NOTHING — it is not "done".
  // We key on declared/anchored/generator edges, NOT raw edge count, so an incidental auto-detected
  // symlink can't mask an un-wired repo (the onboarding trap: a `latest->v2` symlink flipped status to
  // "GREEN ✓ nothing to do" pre-config). When untracked, status becomes a distinct ORIENTATION state
  // and routes to the wire-up recipe instead of telling a brand-new adopter they are finished.
  const declaredEdges = graph.edges.filter((e) => e.directionBasis !== 'symlink').length
  const tracking = declaredEdges > 0
  const gateGreen = built && inSync && policyOk && extOk && !buildErrs && tracking
  const impactPending = mech.length > 0 || adv.length > 0
  // `green` (the structured "nothing to do" signal) MUST include tracking and pending impact work, so
  // an agent branching on `.green` is not told "done" while nextCommands still ask it to regenerate/review.
  const green = gateGreen && !impactPending
  const hasConfig = existsSync(resolve(ROOT, '.true-up.json'))
  const nextCommands = []
  if (!tracking) {
    if (!hasConfig) nextCommands.push(`${cmdHint} init                 # scaffold .true-up.json (zones + seed edges)`)
    nextCommands.push('# declare your first edge in .true-up.json "seed": [{ "from": "<doc>", "to": "<source-of-truth>", "kind": "derives-facts-from" }]')
    nextCommands.push(`${cmdHint} robot-docs           # the full wire-up recipe (seed / span anchor / generated-from)`)
  }
  if (buildErrs) nextCommands.push(`${cmdHint} build               # FIX fail-loud errors first (see buildErrors)`)
  else if (!built) nextCommands.push(`${cmdHint} build               # build the graph${tracking ? ' first' : ' (after wiring an edge above)'}`)
  else if (!inSync) nextCommands.push(`${cmdHint} build               # graph is stale on disk — rebuild`)
  if (mech.length) nextCommands.push(`${cmdHint} run --since ${sinceArg}   # regenerate ${mech.length} mechanical dependent(s)`)
  if (adv.length) nextCommands.push(`${cmdHint} --impact --since ${sinceArg} --proof --json   # review ${adv.length} advisory dependent(s); proof marks already-edited files`)
  if (!policyOk) nextCommands.push(`${cmdHint} --policy             # fix policy violations`)
  if (!extOk) nextCommands.push(`${cmdHint} --externalities      # fix machine-local-path leaks`)
  if (!nextCommands.length) nextCommands.push('(green — nothing to do)')
  const payload = {
    ok: true, _v: CONTRACT_VERSION, built, stale: !inSync, staleReason,
    graph: { nodes: Object.keys(graph.nodes).length, factNodes: Object.keys(graph.nodes).filter((k) => k.startsWith('fact:')).length, edges: graph.edges.length, declaredEdges, inert: !graph.edges.length, tracking },
    workspace,
    since,
    impact: {
      changedFacts: changedFacts.map((f) => f.split('#')[1] || f),
      mechanical: mech.map((h) => ({ node: h.node.replace(/^file:/, ''), via: h.via || null })),
      advisory: adv.map((h) => ({ doc: h.node.replace(/^file:/, ''), from: h.fromSource.replace(/^fact:/, '') })),
    },
    policy: { ok: policyOk }, externalities: { ok: extOk },
    green, gateGreen, tracking, impactPending,
    buildErrors: buildErrs ? { anchors: graph.anchorErrors, seeds: graph.seedErrors, imports: graph.importErrors || [], spans: graph.spanErrors || [] } : null,
    nextCommands,
  }
  if (JSONOUT) jout(payload)
  else {
    console.log(`true-up status (${rel(ROOT) || '.'}) — read-only orientation\n`)
    const vcsDetail = workspace.vcs === 'git' && workspace.git?.linkedWorktree ? 'git linked-worktree'
      : workspace.vcs === 'git' && workspace.jj?.colocated ? 'git-backed colocated jj'
        : workspace.vcs
    console.log(`  target:   ${workspace.root}`)
    console.log(`  selected: ${workspace.repoSource}${workspace.requestedWasSubdir ? ` (${workspace.requestedRelative} → repo root)` : ''}`)
    console.log(`  cwd:      ${workspace.cwdRelative || workspace.cwd}`)
    console.log(`  vcs:      ${vcsDetail} · default since ${since}`)
    if (workspace.warnings.length) for (const w of workspace.warnings) console.log(`  warning:  ${w.message}`)
    console.log(`  graph:    ${payload.graph.nodes} nodes, ${payload.graph.edges} edges${!tracking ? ' (INERT — nothing declared to track yet)' : ''}`)
    console.log(`  built:    ${built ? (inSync ? 'yes, in sync' : 'yes, STALE on disk') : 'NO — not built'}`)
    if (buildErrs) console.log(`  errors:   ${buildErrs} fail-loud build error(s) — run a build to see them`)
    console.log(`  changed:  since ${since} → ${mech.length} mechanical / ${adv.length} advisory dependent(s)`)
    if (adv.length) for (const h of adv) console.log(`              advisory: ${h.node.replace(/^file:/, '')}  ←  ${h.fromSource.replace(/^fact:/, '')}`)
    console.log(`  policy:   ${policyOk ? 'clean' : 'VIOLATIONS'} · externalities: ${extOk ? 'clean' : 'LEAKS'}`)
    const verdict = !tracking ? 'SET UP — but TRACKING NOTHING yet (declare your first edge ↓)' : (green ? 'GREEN ✓ (nothing to do)' : 'work pending ↓')
    console.log(`  verdict:  ${verdict}`)
    console.log('\n  next:')
    for (const c of nextCommands) console.log(`    ${c}`)
    console.log('\n  (read-only probe — wrote nothing. JSON: true-up status --json)')
  }
  process.exit(0)
}

