writeFileSync(OUT, serialize(graph))
const nNodes = Object.keys(graph.nodes).length
const nFacts = Object.keys(graph.nodes).filter((k) => k.startsWith('fact:')).length
const byBasis = graph.edges.reduce((a, e) => ((a[e.directionBasis] = (a[e.directionBasis] || 0) + 1), a), {})
const inert = !graph.edges.length || !Object.keys(FACTS).length
if (JSONOUT) jout({ out: rel(OUT), nodes: nNodes, factNodes: nFacts, edges: graph.edges.length, byDirectionBasis: byBasis, inert })
else {
  console.log(`${rel(OUT)} written: ${nNodes} nodes (${nFacts} fact-nodes), ${graph.edges.length} edges`)
  console.log(`  edges by direction-basis: ${JSON.stringify(byBasis)}`)
  // Empty-graph notice: distinguish "nothing changed" from "you declared nothing to track" (report LIMITATION).
  if (inert) console.error(`\nNOTICE: no ${!Object.keys(FACTS).length ? 'steward facts' : 'edges'} declared — the drift-detection layer is INERT (it passes --check trivially). Declare facts/seed in .true-up.json (see docs/CONFIG.md). Scope: only JSON files with top-level arrays-of-objects can be stewards; code files (e.g. .py) are valid seed-edge endpoints but are not auto-decomposed into facts.`)
}
