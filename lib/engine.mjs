const lev = (a, b) => {
  const d = Array.from({ length: a.length + 1 }, (_, i) => [i, ...Array(b.length).fill(0)])
  for (let j = 0; j <= b.length; j++) d[0][j] = j
  for (let i = 1; i <= a.length; i++) for (let j = 1; j <= b.length; j++) d[i][j] = Math.min(d[i - 1][j] + 1, d[i][j - 1] + 1, d[i - 1][j - 1] + (a[i - 1] === b[j - 1] ? 0 : 1))
  return d[a.length][b.length]
}
const bare = (s) => s.replace(/^--?/, '') // strip a leading -- so a cross-prefix typo ('imapct' vs '--impact') still scores
const didYouMean = (w) => {
  if (SYNONYMS[w]) return SYNONYMS[w] // semantic guess wins over lexical distance
  const pool = w.startsWith('--') ? [...KNOWN_CMDS, ...GLOBAL_FLAGS] : KNOWN_CMDS // a --typo may be a global flag
  let best = null, bd = 99
  for (const c of pool) { const dd = lev(bare(w), bare(c)); if (dd < bd) { bd = dd; best = c } }
  return bd <= 3 ? best : null
}

// Per-command flag validation (Axiom 7): a typo'd flag is REJECTED with a did-you-mean, never silently
// dropped. This closes a real safety hole — `--check --comitted` otherwise falls through to the
// worktree check, silently downgrading the committed drift gate (exit 1→0) — and stops `--impact`'s
// typo'd flag from becoming a bogus positional target ("0 dependents, exit 0"). --repo/--json are
// already stripped from `argv`, so this only sees command-scoped flags. Runs before dispatch.
const CMD_FLAGS = {
  '--check': ['--committed'], '--impact': ['--since', '--proof'], '--policy': ['--report'],
  '--externalities': ['--report'], '--verify-scope': ['--since'], run: ['--since', '--strict'],
  gate: ['--committed'], hooks: ['--install', '--uninstall', '--ci', '--force'], export: ['--audience'],
  status: ['--since'], graph: [], build: [], 'robot-docs': [],
}
const VALUE_FLAGS = new Set(['--since', '--audience']) // consume the following token as a value, not a flag
// Commands that take NO positional argument — a stray positional must be a usage ERROR, never silently
// swallowed (Axiom 14). `gate zzz` used to report PASS exit 0; the worst possible false-positive in CI.
// (--impact is the only positional-taking command, so it's excluded.)
const NO_POSITIONAL = new Set(Object.keys(CMD_FLAGS).filter((k) => k !== '--impact'))
const nearestFlag = (w, allowed) => { let best = null, bd = 99; for (const f of allowed) { const d = lev(w, f); if (d < bd) { bd = d; best = f } } return bd <= 3 ? best : null }
{
  const cmd = (!argv[0] || argv[0] === '--check') ? '--check' : argv[0]
  if (CMD_FLAGS[cmd] !== undefined) { // a flag-taking command (incl. bare build, validated as --check)
    const allowed = new Set(CMD_FLAGS[cmd])
    for (let i = 1; i < argv.length; i++) {
      const a = argv[i]
      if (!a.startsWith('--')) { // a value (consumed above) or a positional
        if (NO_POSITIONAL.has(cmd)) jerr(`unexpected argument: ${a} — '${cmd}' takes no positional arguments`, 2, { kind: 'unexpected-arg', command: cmd, got: a })
        continue // positional target (--impact only)
      }
      if (allowed.has(a)) { if (VALUE_FLAGS.has(a)) i++; continue }
      const guess = nearestFlag(a, [...allowed, ...GLOBAL_FLAGS]) // a typo may be a GLOBAL flag (--jsno → --json)
      const where = !argv[0] ? 'true-up (build)' : cmd
      jerr(`unknown flag for ${where}: ${a}${guess ? `\ndid you mean: ${guess} ?` : ''}\nrun: ${rebuildHint} --help   (or ${rebuildHint} capabilities)`, 2, { kind: 'unknown-flag', command: where, given: a, didYouMean: guess || null })
    }
  }
}

