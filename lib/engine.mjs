// Intent inference (Axiom 7): a mistyped OR semantically-guessed command gets a surgical redirect, never
// a dead end. KNOWN_CMDS feeds lexical did-you-mean; SYNONYMS maps the verbs a fresh agent actually
// guesses ('status'/'update'/'docs') to the canonical command — because lexical distance points at the
// wrong verb for an intent guess ('update'→'gate' is lev-near but semantically 'run').
const GLOBAL_FLAGS = ['--json', '--no-write', '--repo']
const KNOWN_CMDS = ['--check', '--impact', '--policy', '--externalities', 'run', 'gate', 'hooks', 'export', '--verify-scope', 'init', 'capabilities', 'status', 'graph', 'build', 'robot-docs', '--version', 'version', '--help', 'help']
