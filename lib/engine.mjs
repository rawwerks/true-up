// Per-repo adoption: wire a pre-commit + pre-push gate (the gate FAILS CLOSED if the tool is absent —
// a gate that cannot run must block, not silently pass), or print a CI snippet. NB jj commit bypasses
// pre-commit, so the gate also lives on pre-push.
if (argv[0] === 'hooks') {
  const gx = (...a) => execFileSync('git', ['-C', ROOT, ...a], { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim()
  let hooksDir, gitDir, commonDir
  try { hooksDir = resolve(ROOT, gx('rev-parse', '--git-path', 'hooks')); gitDir = resolve(ROOT, gx('rev-parse', '--absolute-git-dir')); commonDir = resolve(ROOT, gx('rev-parse', '--git-common-dir')) }
  catch { console.error('not a git repository — hooks need a .git directory'); process.exit(2) }
  // Is the resolved hooks dir INSIDE this repo's own .git (or its worktree common dir)? If not,
  // core.hooksPath points at a SHARED location (commonly a GLOBAL ~/.config/git/hooks) — writing there
  // silently rewires EVERY repo on the machine, and a clobbered foreign hook is only recoverable from
  // its .bak. We refuse such writes unless --force. (This is the exact footgun that overwrote a
  // developer's global pre-commit/pre-push during `npm test` before the harness was isolated.)
  const within = (d) => hooksDir === resolve(d, 'hooks') || hooksDir.startsWith(resolve(d, 'hooks') + '/') || hooksDir === d || hooksDir.startsWith(d + '/')
  const external = !(within(gitDir) || within(commonDir))
  const force = argv.includes('--force')
  const showDir = external ? hooksDir : rel(hooksDir) // absolute for a shared dir so its scope is unmistakable
  const MARK = 'managed-by: true-up-hooks'
  const hookBody = (which) => `#!/bin/sh
# true-up ${which} gate — ${MARK}. Edit freely; 'true-up hooks --install' refreshes, '--uninstall' removes.
# Remove by hand: delete this file from your git hooks dir ('git rev-parse --git-path hooks').
if ! command -v true-up >/dev/null 2>&1; then
  echo "true-up: not on PATH — install it (https://github.com/rawwerks/true-up) or run 'true-up hooks --uninstall'" >&2
  exit 1
fi
true-up --policy        || exit 1
true-up --externalities || exit 1
# Drift gate too — if you build/track the dependency graph, swap the two lines above for the all-in-one:
#   true-up gate            # = --check + --policy + --externalities   (add --committed if you commit the graph)
`
  const ci = `# .github/workflows/true-up.yml — pin the version so the gate is deterministic across runs
name: true-up
on: [push, pull_request]
jobs:
  true-up:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-node@v4
        with: { node-version: '20' }
      - run: npx true-up@${VERSION} --policy
      - run: npx true-up@${VERSION} --externalities
      # - run: npx true-up@${VERSION} gate --committed   # if you build + commit the dependency graph
`
  if (argv.includes('--ci')) { if (JSONOUT) jout({ ci }); else process.stdout.write(ci); process.exit(0) }
  // Refuse to write a hooks dir OUTSIDE this repo's .git unless --force — guards both --install and
  // --uninstall so an accidental uninstall can't wipe a shared/global dir either.
  if (external && !force && (argv.includes('--install') || argv.includes('--uninstall'))) {
    const verb = argv.includes('--uninstall') ? 'uninstall from' : 'install into'
    const m = `true-up: refusing to ${verb} a hooks dir OUTSIDE this repo:\n  ${hooksDir}\ncore.hooksPath points hooks at a SHARED location — this affects ALL your repos, not just this one.\nScope to THIS repo:  git -C ${rel(ROOT) || '.'} config --local core.hooksPath .git/hooks\nOr pass --force if you really intend to change the shared/global hooks.`
    if (JSONOUT) jout({ ok: false, error: 'hooks-dir-outside-repo', hooksDir, external: true, hint: m })
    else console.error(m)
    process.exit(2)
  }
  if (argv.includes('--uninstall')) {
    const removed = [], restored = []
    for (const h of ['pre-commit', 'pre-push']) {
      const p = resolve(hooksDir, h)
      try { if (existsSync(p) && readFileSync(p, 'utf8').includes(MARK)) { unlinkSync(p); removed.push(h) } } catch {}
      // Restore the foreign hook we backed up on install — otherwise uninstall leaves the user with NO
      // active hook (the silent-loss bug). Only restore when our hook is gone and a .bak exists.
      try { if (existsSync(p + '.bak') && !existsSync(p)) { copyFileSync(p + '.bak', p); chmodSync(p, 0o755); unlinkSync(p + '.bak'); restored.push(h) } } catch {}
    }
    if (JSONOUT) jout({ removed, restored, hooksDir: showDir })
    else { console.log(removed.length ? `removed true-up hooks: ${removed.join(', ')}` : 'no true-up-managed hooks to remove'); if (restored.length) console.log(`  restored pre-existing hook(s) from backup: ${restored.join(', ')}`) }
    process.exit(0)
  }
  if (argv.includes('--install')) {
    mkdirSync(hooksDir, { recursive: true })
    const installed = [], backedUp = []
    for (const h of ['pre-commit', 'pre-push']) {
      const p = resolve(hooksDir, h)
      // Back up a pre-existing FOREIGN hook ONCE: never overwrite an existing .bak (a refresh cycle would
      // otherwise replace the genuine original with our own managed hook), and never back up our own hook.
      if (existsSync(p)) { let cur = ''; try { cur = readFileSync(p, 'utf8') } catch {}; if (!cur.includes(MARK) && !existsSync(p + '.bak')) { try { copyFileSync(p, p + '.bak'); backedUp.push(rel(p + '.bak')) } catch {} } }
      writeFileSync(p, hookBody(h)); chmodSync(p, 0o755); installed.push(h)
    }
    if (JSONOUT) jout({ installed, backedUp, hooksDir: showDir, external, gate: ['--policy', '--externalities'] })
    else {
      console.log(`installed true-up hooks (${installed.join(', ')}) in ${showDir}`)
      if (external) console.error('  NOTE: SHARED hooks dir (core.hooksPath) — this affects ALL your repos (installed with --force).')
      if (backedUp.length) console.error(`  backed up pre-existing hook(s): ${backedUp.join(', ')} — restored automatically on --uninstall`)
      console.log('  gate: true-up --policy && true-up --externalities  (edit the hook for `true-up gate` drift gating)')
    }
    process.exit(0)
  }
  // default: dry plan (writes nothing)
  if (JSONOUT) jout({ hooksDir: showDir, external, wouldInstall: ['pre-commit', 'pre-push'], gate: ['--policy', '--externalities'], actions: { install: 'hooks --install', ci: 'hooks --ci', uninstall: 'hooks --uninstall' } })
  else {
    console.log('true-up hooks (dry run — nothing written):')
    console.log(`  would install pre-commit + pre-push in ${showDir}`)
    if (external) console.log('  WARNING: that is a SHARED hooks dir (core.hooksPath) affecting ALL repos — install requires --force')
    console.log('  gate: true-up --policy && true-up --externalities')
    console.log('  --install to write · --ci for a CI snippet · --uninstall to remove')
  }
  process.exit(0)
}

// --- change detection (repo-agnostic: VCS + per-fact hashing; no repo-specific names) ---
// Facts of a steward file as it stood at <ref>, for since-comparison. (stderr suppressed: a file absent
// at <ref> is a normal "no facts then" answer, not an error to surface.)
function factsAt(ref, path) {
  return extractFacts(path, vcsShow(ref, path))
}
// Span facts of a file as it stood at <ref>, for since-comparison (Tier 1 early-cutoff).
function spansAt(ref, path) {
  return extractSpans(path, vcsShow(ref, path)).facts
}
// File content as it stood at <ref> ('' if absent) — for Tier 2 symbol since-comparison.
// Seeds for explicit targets (paths or path#fact); steward facts compared working-tree vs the committed graph.
function seedsFromTargets(targets, graph) {
  const seeds = new Set()
  const changedFacts = []
  const unknownTargets = [] // a typo'd path/fact must be a usage ERROR, not a silent "0 dependents, exit 0"
  for (const t of targets) {
    const imported = parseImportedTarget(t)
    if (imported) {
      const id = importedNodeId(imported.alias, imported.id)
      if (graph.nodes[id]) seeds.add(id)
      else unknownTargets.push(t)
      continue
    }
    if (t.includes('#')) {
      const id = 'fact:' + t
      if (graph.nodes[id]) seeds.add(id)
      else unknownTargets.push(t)
      continue
    }
    let matched = false
    if (graph.nodes['file:' + t]) { seeds.add('file:' + t); matched = true }
    for (const id of Object.keys(graph.nodes)) if (id.startsWith(`block:${t}#`)) { seeds.add(id); matched = true }
    if (FACTS[t]) {
      matched = true
      let cur = ''
      try { cur = readFileSync(resolve(ROOT, t), 'utf8') } catch {}
      for (const [fid, h] of Object.entries(extractFacts(t, cur))) if (graph.nodes[fid] && graph.nodes[fid].hash !== h) { seeds.add(fid); changedFacts.push(fid) }
    }
    const importCfg = importConfigs().imports.filter((imp) => imp.path === t)
    if (importCfg.length) {
      matched = true
      let cur = ''
      try { cur = readFileSync(resolve(ROOT, t), 'utf8') } catch {}
      for (const [iid, h] of Object.entries(extractImportedFactsForPath(t, cur))) if (graph.nodes[iid] && graph.nodes[iid].hash !== h) { seeds.add(iid); changedFacts.push(iid) }
    }
    if (!matched) unknownTargets.push(t)
  }
  return { seeds, changedFacts, unknownTargets }
}
// Seeds for everything changed since <ref>; steward facts compared at <ref> vs now (early-cutoff).
function seedsForChange(ref, paths, graph) {
  const seeds = new Set()
  const changedFacts = []
  for (const t of paths) {
    if (graph.nodes['file:' + t]) seeds.add('file:' + t)
    for (const id of Object.keys(graph.nodes)) if (id.startsWith(`block:${t}#`)) seeds.add(id)
    if (FACTS[t]) {
      let cur = ''
      try { cur = readFileSync(resolve(ROOT, t), 'utf8') } catch {}
      const now = extractFacts(t, cur)
      const old = factsAt(ref, t)
      for (const fid of new Set([...Object.keys(now), ...Object.keys(old)])) if (now[fid] !== old[fid]) { if (graph.nodes[fid]) seeds.add(fid); changedFacts.push(fid) }
    }
    const importCfg = importConfigs().imports.filter((imp) => imp.path === t)
    if (importCfg.length) {
      let curImp = ''
      try { curImp = readFileSync(resolve(ROOT, t), 'utf8') } catch {}
      const nowI = extractImportedFactsForPath(t, curImp)
      const oldI = extractImportedFactsForPath(t, vcsShow(ref, t))
      for (const iid of new Set([...Object.keys(nowI), ...Object.keys(oldI)])) if (nowI[iid] !== oldI[iid]) { if (graph.nodes[iid]) seeds.add(iid); changedFacts.push(iid) }
    }
    // span facts (Tier 1, any language): compare this file's span-fact hashes now vs at <ref>, so a
    // doc anchored to a code span is stale only when THAT span's bytes move (early-cutoff).
    let curSpan = ''
    try { curSpan = readFileSync(resolve(ROOT, t), 'utf8') } catch {}
    if (curSpan.includes('true-up:anchor') || Object.keys(graph.nodes).some((k) => k.startsWith(`fact:${t}#`))) {
      const nowS = extractSpans(t, curSpan).facts
      const oldS = spansAt(ref, t)
      for (const fid of new Set([...Object.keys(nowS), ...Object.keys(oldS)])) if (nowS[fid] !== oldS[fid]) { if (graph.nodes[fid]) seeds.add(fid); changedFacts.push(fid) }
    }
    // symbol facts (Tier 2): compare tree-sitter symbol hashes now vs at <ref> (symbol-granular early-cutoff)
    if (SYMBOLS && symbolExts().some((e) => t.endsWith(e))) {
      const nowY = extractSymbols(t, curSpan)
      const oldY = extractSymbols(t, vcsShow(ref, t))
      for (const fid of new Set([...Object.keys(nowY), ...Object.keys(oldY)])) if (nowY[fid] !== oldY[fid]) { if (graph.nodes[fid]) seeds.add(fid); changedFacts.push(fid) }
    }
  }
  return { seeds, changedFacts }
}

