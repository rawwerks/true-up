function vcsFiles() {
  if (VCS_KIND === 'jj') return [...new Set(jjLines(['file', 'list']))].sort()
  return [...new Set([...gitLines(['ls-files']), ...gitLines(['ls-files', '--others', '--exclude-standard'])])].sort()
}
function vcsTrackedFiles() {
  if (VCS_KIND === 'jj') return [...new Set(jjLines(['file', 'list']))].sort()
  return [...new Set(gitLines(['ls-files']))].sort()
}
function vcsAnchorFiles() {
  if (VCS_KIND === 'jj') return [...new Set(jjLines(['file', 'search', '--pattern', '*true-up:anchor*']))].sort()
  return [...new Set(gitLines(['grep', '--untracked', '-I', '-lFe', 'true-up:anchor']))].sort()
}
function vcsChanged(ref) {
  if (VCS_KIND === 'jj') return [...new Set(jjLines(['diff', '--from', ref, '--to', '@', '--name-only']))]
  const g = (a) => gitLines(a)
  return [...new Set([...g(['diff', '--name-only', ref, 'HEAD']), ...g(['diff', '--name-only']), ...g(['diff', '--name-only', '--cached']), ...g(['ls-files', '--others', '--exclude-standard'])])]
}
function vcsRefExists(ref) {
  try {
    if (VCS_KIND === 'jj') jjText(['log', '-r', ref, '-n', '1', '--no-graph', '-T', 'commit_id'])
    else execFileSync('git', ['-C', ROOT, 'rev-parse', '--verify', '--quiet', ref + '^{commit}'], { stdio: 'ignore' })
    return true
  } catch { return false }
}
function vcsShow(ref, path) {
  try {
    if (VCS_KIND === 'jj') return jjText(['file', 'show', '-r', ref, path])
    return gitText(['show', `${ref}:${path}`])
  } catch { return '' }
}
function vcsCommittedBlob(path) {
  if (VCS_KIND === 'jj') {
    try { return jjText(['file', 'show', '-r', '@', path]) } catch { return null }
  }
  const blob = (sp) => { try { return gitText(['show', sp]) } catch { return null } }
  return blob(':' + path) ?? blob('HEAD:' + path)
}
function refLabel() { return VCS_KIND === 'jj' ? 'jj revset' : 'git ref' }
function defaultSince() {
  if (VCS_KIND === 'jj') return vcsRefExists('@-') ? '@-' : '@'
  return vcsRefExists('HEAD~1') ? 'HEAD~1' : 'HEAD'
}
function inside(parent, child) {
  const r = relative(parent, child)
  return r === '' || (!r.startsWith('..') && !r.startsWith('/'))
}
function workspaceInfo(since = null) {
  const cwd = resolve(process.cwd())
  const requested = _rr.requested || cwd
  const cwdProbe = probeVcsRoot(cwd)
  const requestedRel = inside(ROOT, requested) ? (relative(ROOT, requested) || '.') : null
  const cwdRel = inside(ROOT, cwd) ? (relative(ROOT, cwd) || '.') : null
  const warnings = []
  if (cwdProbe && resolve(cwdProbe.root) !== resolve(ROOT)) {
    warnings.push({
      kind: 'cwd-target-mismatch',
      message: `${_rr.source} targets a different repo than the current shell directory`,
      cwdRoot: cwdProbe.root,
      targetRoot: ROOT,
    })
  }
  const info = {
    vcs: VCS_KIND,
    repoSource: _rr.source,
    requested,
    requestedRelative: requestedRel,
    requestedWasSubdir: requestedRel !== null && requestedRel !== '.',
    root: ROOT,
    cwd,
    cwdRelative: cwdRel,
    out: rel(OUT),
    since: since || defaultSince(),
    warnings,
  }
  if (VCS_KIND === 'git') {
    const g = (...a) => { try { return execFileSync('git', ['-C', ROOT, ...a], { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim() } catch { return null } }
    const gitDir = g('rev-parse', '--absolute-git-dir')
    const commonRaw = g('rev-parse', '--git-common-dir')
    const commonDir = commonRaw ? resolve(ROOT, commonRaw) : null
    const branch = g('symbolic-ref', '--quiet', '--short', 'HEAD') || g('rev-parse', '--short', 'HEAD')
    info.git = { gitDir, commonDir, linkedWorktree: !!(gitDir && commonDir && resolve(gitDir) !== resolve(commonDir)), branch }
    // A colocated jj repo is intentionally handled through Git plumbing for committed/staged graph
    // checks, but exposing the presence of .jj prevents jj users from thinking true-up missed their
    // workspace. Non-colocated jj repos report VCS_KIND=jj instead.
    info.jj = existsSync(resolve(ROOT, '.jj')) ? { colocated: true, mode: 'git-backed' } : null
  } else if (VCS_KIND === 'jj') {
    const j = (a) => { try { return jjText(a).trim() } catch { return null } }
    info.git = null
    info.jj = {
      colocated: false,
      changeId: j(['log', '-r', '@', '-n', '1', '--no-graph', '-T', 'change_id.short()']),
      commitId: j(['log', '-r', '@', '-n', '1', '--no-graph', '-T', 'commit_id.short()']),
    }
  }
  return info
}
function filesUnder(prefix) {
  if (!prefix) return vcsFiles()
  const p = prefix.endsWith('/') ? prefix : prefix + '/'
  return vcsFiles().filter((f) => f === prefix || f.startsWith(p))
}

// The file universe true-up nodes by default: tracked text artifacts that commonly carry repo
// truth (docs, config/data, workflows, scripts, and source), minus VCS/vendor/private/generated cache.
// This boundary is intentionally repo-agnostic: a seed can still force any tracked file node, but an
// agent's first `--impact install.sh`/`graph`/`--verify-scope` should not fail just because the file is
// shell, YAML, or an extensionless bin entrypoint. Span/symbol facts can also node code beyond this set.
const KEEP_RE = /\.(md|markdown|txt|json|jsonc|ya?ml|toml|mjs|cjs|js|jsx|ts|tsx|py|rs|go|sh|bash|zsh|rb|java|kt|swift|c|h|cc|cpp|hpp|css|html)$/
const KEEP_BASENAME_RE = /(^|\/)(README|CHANGELOG|PUBLISHING|LICENSE|NOTICE|Makefile|Dockerfile)(\.[^/]*)?$/
const KEEP_SPECIAL_RE = /(^|\/)(\.gitignore|\.npmignore|bun\.lock|package-lock\.json|pnpm-lock\.yaml|yarn\.lock)$/
const KEEP_PATH_RE = /^(bin\/[^/]+|scripts\/[^/]+)$/
const SKIP_RE = /^(private\/|node_modules\/|\.git\/|\.jj\/|\.true-up\/)|(^|\/)bun\.lockb$|(^|\/)depgraph\.(json|seed\.json)$/
const keepFile = (p) => (KEEP_RE.test(p) || KEEP_BASENAME_RE.test(p) || KEEP_SPECIAL_RE.test(p) || KEEP_PATH_RE.test(p) || p === '.true-up.json' || p === 'true-up.config.json') && !SKIP_RE.test(p)
function listFiles() {
  const all = vcsFiles()
  return all.filter(keepFile).sort()
}

const extKind = (p) => /(^bin\/|^scripts\/|\.(mjs|cjs|js|jsx|py|ts|tsx|rb|go|rs|sh|bash|zsh|c|cc|cpp|h|java|kt|swift)$)/.test(p) ? 'code'
  : /\.(json|jsonc|ya?ml|toml)$/.test(p) || KEEP_SPECIAL_RE.test(p) || p === '.true-up.json' || p === 'true-up.config.json' ? 'data'
  : 'prose'

function classify(path, content) {
  if (extKind(path) === 'code') return { kind: 'code', role: 'none' }
  if (extKind(path) === 'data') {
    if (FACTS[path]) return { kind: 'data', role: 'steward' }
    return { kind: 'data', role: 'none' }
  }
  if (/^<!--\s*generated by /m.test(content.slice(0, 400))) return { kind: 'generated', role: 'guard' }
  if (path.endsWith('SKILL.md')) return { kind: 'prose', role: 'writer', audience: 'external-agents' }
  if (path === 'README.md' || /\/README\.md$/.test(path)) return { kind: 'prose', role: 'writer', audience: 'users' }
  if (path === 'AGENTS.md') return { kind: 'prose', role: 'writer', audience: 'maintainers' }
  return { kind: 'prose', role: 'writer', audience: 'mixed' }
}

// Resolve a generator's input file from its `here('./X.json')` literal, relative to the
// generator's own directory (not a hardcoded prefix), so generators live anywhere.
function generatorSource(genRel) {
  try {
    const m = readFileSync(resolve(ROOT, genRel), 'utf8').match(/here\(['"]\.\/([\w.\-]+\.json)['"]\)/)
    if (m) return rel(resolve(ROOT, dirname(genRel), m[1]))
  } catch {}
  return null
}

function mkEdge(from, to, kind, directionBasis, propagation, granularity, via) {
  return { from, to, kind, directionBasis, propagation, granularity, ...(via ? { via } : {}) }
}

function generatorInvocation(genAbs) {
  if (/\.(mjs|cjs|js)$/.test(genAbs)) return { cmd: 'node', args: [genAbs] }
  if (/\.(sh|bash)$/.test(genAbs)) return { cmd: 'bash', args: [genAbs] }
  if (/\.zsh$/.test(genAbs)) return { cmd: 'zsh', args: [genAbs] }
  if (/\.py$/.test(genAbs)) return { cmd: 'python3', args: [genAbs] }
  if (/\.rb$/.test(genAbs)) return { cmd: 'ruby', args: [genAbs] }
  if (/\.pl$/.test(genAbs)) return { cmd: 'perl', args: [genAbs] }
  return { cmd: genAbs, args: [] }
}

const IMPORT_ALIAS_RE = /^[A-Za-z0-9][A-Za-z0-9._-]*$/
const EXPORT_ID_RE = /^[A-Za-z0-9][A-Za-z0-9._-]*$/
const RESERVED_IMPORT_ALIASES = new Set(['import', 'imports', 'export', 'exports', 'default', 'main'])
const importedNodeId = (alias, id) => `import:@${alias}:${id}`
function parseImportedTarget(s) {
  if (typeof s !== 'string' || !s.startsWith('@')) return null
  const i = s.indexOf(':')
  if (i <= 1) return null
  return { alias: s.slice(1, i), id: s.slice(i + 1) }
}
function validateImportAlias(alias) {
  return IMPORT_ALIAS_RE.test(alias) && !alias.includes('..') && !RESERVED_IMPORT_ALIASES.has(alias)
}
function readImportSnapshot(path) {
  try {
    const content = readFileSync(resolve(ROOT, path), 'utf8')
    const data = JSON.parse(content)
    return { content, data }
  } catch (e) {
    return { error: (e && e.message) || String(e) }
  }
}
function importConfigs(tracked = new Set(vcsTrackedFiles())) {
  const out = []
  const errors = []
  if (IMPORTS !== undefined && (typeof IMPORTS !== 'object' || IMPORTS === null || Array.isArray(IMPORTS))) {
    errors.push({ reason: 'imports must be an object' })
    return { imports: out, errors }
  }
  for (const [alias, spec] of Object.entries(IMPORTS || {})) {
    if (!validateImportAlias(alias)) { errors.push({ alias, reason: 'bad import alias' }); continue }
    if (!spec || typeof spec !== 'object' || Array.isArray(spec)) {
      errors.push({ alias, reason: 'import must be an object with path, repoId, and audience pins' })
      continue
    }
    const rawPath = typeof spec.path === 'string' ? spec.path : null
    if (!rawPath) { errors.push({ alias, reason: 'import path missing' }); continue }
    const abs = resolve(ROOT, rawPath)
    const relPath = relative(ROOT, abs)
    if (relPath === '' || relPath.startsWith('..') || relPath.startsWith('/')) {
      errors.push({ alias, path: rawPath, reason: 'import snapshot path outside repo' })
      continue
    }
    if (!tracked.has(relPath)) {
      errors.push({ alias, path: relPath, reason: 'import snapshot not tracked / not found (track/stage or commit the local snapshot)' })
      continue
    }
    try {
      if (lstatSync(abs).isSymbolicLink()) {
        errors.push({ alias, path: relPath, reason: 'import snapshot must be a regular in-repo file, not a symlink' })
        continue
      }
    } catch {
      errors.push({ alias, path: relPath, reason: 'import snapshot not tracked / not found (track/stage or commit the local snapshot)' })
      continue
    }
    const expectedRepoId = typeof spec.repoId === 'string' && spec.repoId ? spec.repoId : null
    const expectedAudience = typeof spec.audience === 'string' ? spec.audience : null
    if (!expectedRepoId) { errors.push({ alias, path: relPath, reason: 'import must pin repoId' }); continue }
    if (!expectedAudience) { errors.push({ alias, path: relPath, reason: 'import must pin audience' }); continue }
    if (!isAudienceLevel(expectedAudience)) { errors.push({ alias, path: relPath, reason: `unknown import audience pin: ${expectedAudience}` }); continue }
    out.push({ alias, path: relPath, expectedRepoId, expectedAudience })
  }
  return { imports: out, errors }
}
function importFactsFromSnapshot(alias, path, data, expected = {}) {
  const errors = []
  const facts = {}
  if (!data || data.kind !== 'true-up-import-snapshot' || typeof data !== 'object') {
    errors.push({ alias, path, reason: 'not a true-up import snapshot' })
    return { facts, errors, meta: {} }
  }
  const topKeys = new Set(['kind', '_v', 'ok', 'repoId', 'audience', 'facts'])
  for (const k of Object.keys(data)) if (!topKeys.has(k)) errors.push({ alias, path, reason: 'unsupported snapshot key' })
  if (!data.repoId || typeof data.repoId !== 'string') errors.push({ alias, path, reason: 'snapshot missing repoId' })
  if (!data.audience || typeof data.audience !== 'string') errors.push({ alias, path, reason: 'snapshot missing audience' })
  else if (!isAudienceLevel(data.audience)) errors.push({ alias, path, reason: 'unknown snapshot audience' })
  if (expected.expectedRepoId && data.repoId !== expected.expectedRepoId) errors.push({ alias, path, reason: 'repoId mismatch' })
  if (expected.expectedAudience && data.audience !== expected.expectedAudience) errors.push({ alias, path, reason: 'audience mismatch' })
  if (!data.facts || typeof data.facts !== 'object' || Array.isArray(data.facts)) {
    errors.push({ alias, path, reason: 'snapshot facts must be an object' })
    return { facts, errors, meta: data }
  }
  for (const [id, f] of Object.entries(data.facts)) {
    if (!EXPORT_ID_RE.test(id)) { errors.push({ alias, path, reason: 'bad imported fact id' }); continue }
    if (!f || typeof f !== 'object' || typeof f.hash !== 'string') { errors.push({ alias, path, reason: 'imported fact missing hash' }); continue }
    const factKeys = new Set(['hash', 'audience', 'visibility', 'kind', 'taint'])
    for (const k of Object.keys(f)) if (!factKeys.has(k)) errors.push({ alias, path, reason: 'unsupported imported fact key' })
    const audience = typeof f.audience === 'string' ? f.audience : (typeof data.audience === 'string' ? data.audience : 'internal')
    const visibility = typeof f.visibility === 'string' ? f.visibility : audience
    if (!isAudienceLevel(audience)) { errors.push({ alias, path, reason: 'unknown imported fact audience' }); continue }
    if (!isAudienceLevel(visibility)) { errors.push({ alias, path, reason: 'unknown imported fact visibility' }); continue }
    const taint = []
    for (const t of Array.isArray(f.taint) ? f.taint : []) {
      if (!t || typeof t !== 'object') continue
      const taintKeys = new Set(['alias', 'repoId', 'audience', 'id'])
      for (const k of Object.keys(t)) if (!taintKeys.has(k)) errors.push({ alias, path, reason: 'unsupported imported taint key' })
      if (t.audience !== undefined && !isAudienceLevel(t.audience)) { errors.push({ alias, path, reason: 'unknown imported taint audience' }); continue }
      if (t.id !== undefined && (typeof t.id !== 'string' || !EXPORT_ID_RE.test(t.id))) { errors.push({ alias, path, reason: 'bad imported taint id' }); continue }
      taint.push({
        ...(typeof t.alias === 'string' ? { alias: t.alias } : {}),
        ...(typeof t.repoId === 'string' ? { repoId: t.repoId } : {}),
        ...(typeof t.audience === 'string' ? { audience: t.audience } : {}),
        ...(typeof t.id === 'string' ? { id: t.id } : {}),
      })
    }
    const effectiveAudience = maxAudienceLevel([audience, visibility, ...taint.map((t) => t.audience)])
    if (audienceRank(effectiveAudience) > audienceRank('public')) {
      taint.push({ alias, repoId: data.repoId || alias, audience: effectiveAudience, id })
    }
    facts[importedNodeId(alias, id)] = {
      kind: 'imported-fact',
      role: 'imported',
      hash: f.hash,
      importAlias: alias,
      importId: id,
      repoId: data.repoId || alias,
      audience,
      visibility,
      effectiveAudience,
      sourceAudience: data.audience || audience,
      snapshot: path,
      importedKind: f.kind || 'imported-fact',
      taint,
    }
  }
  return { facts, errors, meta: data }
}
function extractImportedFactsForPath(path, content) {
  const out = {}
  for (const { alias, path: ipath } of importConfigs().imports) {
    if (ipath !== path) continue
    let data
    try { data = JSON.parse(content || '{}') } catch { data = null }
    const r = importFactsFromSnapshot(alias, ipath, data)
    for (const [id, n] of Object.entries(r.facts)) out[id] = n.hash
  }
  return out
}
function resolveExportSource(graph, source) {
  const imported = parseImportedTarget(source)
  if (imported) return importedNodeId(imported.alias, imported.id)
  if (typeof source === 'string' && source.includes('#')) return 'fact:' + source
  return 'file:' + source
}
function pathFromNodeId(id) {
  if (typeof id !== 'string') return null
  if (id.startsWith('file:')) return id.slice(5)
  if (id.startsWith('fact:')) return id.slice(5).split('#')[0]
  if (id.startsWith('block:')) return id.slice(6).split('#')[0]
  return null
}
function visibilityForNode(graph, nodeId, zones = loadZones()) {
  const n = graph.nodes[nodeId] || {}
  if (nodeId.startsWith('import:')) return effectiveImportedAudience(n)
  if (isAudienceLevel(n.visibility)) return n.visibility
  const p = pathFromNodeId(nodeId)
  if (p) {
    const host = graph.nodes['file:' + p]
    if (host && isAudienceLevel(host.visibility)) return host.visibility
    const z = zoneOf(p, zones)
    if (z && isAudienceLevel(z.visibility)) return z.visibility
  }
  return 'public'
}
function sourceVisibilityForExport(graph, sourceId) {
  return visibilityForNode(graph, sourceId)
}
function computeTaints(graph) {
  const taints = {}
  for (const [id, n] of Object.entries(graph.nodes)) {
    if (Array.isArray(n.taint) && n.taint.length) taints[id] = [...n.taint]
    else taints[id] = []
  }
  const mergeTaints = (target, incoming) => {
    if (!incoming || !incoming.length) return false
    const cur = taints[target] || []
    const seen = new Set(cur.map((t) => `${t.alias || ''}|${t.repoId || ''}|${t.audience || ''}|${t.id || ''}`))
    let changed = false
    for (const t of incoming) {
      const key = `${t.alias || ''}|${t.repoId || ''}|${t.audience || ''}|${t.id || ''}`
      if (seen.has(key)) continue
      cur.push(t); seen.add(key); changed = true
    }
    taints[target] = cur
    return changed
  }
  let changed = true
  while (changed) {
    changed = false
    for (const e of graph.edges) {
      const srcT = taints[e.to] || []
      if (mergeTaints(e.from, srcT)) changed = true
    }
    for (const id of Object.keys(graph.nodes)) {
      if (!id.startsWith('fact:')) continue
      const host = 'file:' + pathFromNodeId(id)
      if (mergeTaints(id, taints[host] || [])) changed = true
    }
  }
  return taints
}

function build() {
  const files = listFiles()
  const nodes = {}
  const edges = []
  const contents = {}
  const anchorErrors = []
  const symlinks = []
  const tracked = new Set(vcsFiles())
  const trackedImports = new Set(vcsTrackedFiles())
  const importErrors = []

  for (const path of files) {
    const abs = resolve(ROOT, path)
    let st
    try { st = lstatSync(abs) } catch { continue }
    if (st.isSymbolicLink()) {
      // a symlink is an ALIAS of its target (e.g. CLAUDE.md -> AGENTS.md): same bytes,
      // one canonical source. Do NOT read the followed content as an independent node.
      let targetRel = null
      try { targetRel = rel(resolve(dirname(abs), readlinkSync(abs))) } catch {}
      nodes['file:' + path] = { kind: 'symlink', role: 'alias', ...(targetRel ? { symlinkTo: targetRel } : {}), hash: sha256('symlink->' + (targetRel || '?')) }
      if (targetRel) symlinks.push({ from: path, to: targetRel })
      continue
    }
    let content
    try { content = readFileSync(abs, 'utf8') } catch { continue }
    contents[path] = content
    const m = classify(path, content)
    nodes['file:' + path] = { kind: m.kind, role: m.role, ...(m.audience ? { audience: m.audience } : {}), hash: sha256(content) }
    // decompose steward data files into per-fact nodes
    for (const [factId, h] of Object.entries(extractFacts(path, content))) {
      nodes[factId] = { kind: 'fact', role: 'steward-fact', hash: h }
    }
  }

  const importCfg = importConfigs(trackedImports)
  importErrors.push(...importCfg.errors)
  for (const imp of importCfg.imports) {
    const snap = readImportSnapshot(imp.path)
    if (snap.error) { importErrors.push({ alias: imp.alias, path: imp.path, reason: snap.error }); continue }
    const r = importFactsFromSnapshot(imp.alias, imp.path, snap.data, imp)
    importErrors.push(...r.errors)
    for (const [id, n] of Object.entries(r.facts)) nodes[id] = n
  }

  // language-agnostic span-fact extraction (Tier 1): paired comment-token anchors make ANY tracked
  // file a source-of-truth. The VCS search finds only files containing the token (bounded read,
  // binary-safe, deterministic); --untracked matches build's inclusion of untracked-non-ignored files.
  const spanErrors = []
  for (const path of vcsAnchorFiles()) {
    if (SKIP_RE.test(path)) continue
    let content
    try { content = readFileSync(resolve(ROOT, path), 'utf8') } catch { continue }
    const { facts, errors } = extractSpans(path, content)
    for (const e of errors) spanErrors.push(e)
    if (!Object.keys(facts).length) continue
    // ensure a host file node exists (code files aren't kept by listFiles's md/json/mjs/js filter)
    if (!nodes['file:' + path]) nodes['file:' + path] = { kind: extKind(path), role: 'source', hash: sha256(content) }
    for (const [factId, h] of Object.entries(facts)) nodes[factId] = { kind: 'fact', role: 'span-fact', hash: h }
  }

  // Tier 2 (opt-in via CONFIG.symbols): tree-sitter symbol facts. Parsers are pre-loaded by
  // initSymbols before dispatch; extractSymbols is a no-op if a grammar didn't load. An explicit span
  // fact (above) wins over a symbol fact of the same id (`if (!nodes[factId])`).
  if (SYMBOLS) {
    const codeFiles = vcsFiles().filter((p) => !SKIP_RE.test(p) && symbolExts().some((e) => p.endsWith(e))).sort()
    for (const path of codeFiles) {
      let content
      try { content = readFileSync(resolve(ROOT, path), 'utf8') } catch { continue }
      const syms = extractSymbols(path, content)
      if (!Object.keys(syms).length) continue
      if (!nodes['file:' + path]) nodes['file:' + path] = { kind: extKind(path), role: 'source', hash: sha256(content) }
      for (const [factId, h] of Object.entries(syms)) if (!nodes[factId]) nodes[factId] = { kind: 'fact', role: 'symbol-fact', hash: h }
    }
  }

  for (const path of files) {
    const content = contents[path]
    if (!content) continue
    if (extKind(path) === 'code') continue // code files carry HTML marker strings as literals; use span/symbol/seed for code facts
    const orig = content.split('\n')
    const sup = suppressor(orig)
    if (fileSuppressed(orig, 'true-up-markers')) continue
    // markers/anchors inside code formatting are docs, not live; line/file suppressions let tests quote
    // marker syntax in prose without turning the fixture source itself into graph data.
    const scan = stripCode(content).split('\n').map((line, i) => sup(i, 'true-up-markers') ? '' : line).join('\n')
    // whole-file generated marker
    const whole = scan.match(/<!--\s*generated by\s+([^\s)]+)(?:\s+from\s+([^\s)]+))?/)
    if (whole && /^<!--\s*generated by /m.test(scan.slice(0, 400))) {
      const source = whole[2] || generatorSource(whole[1])
      if (source && nodes['file:' + source]) edges.push(mkEdge('file:' + path, 'file:' + source, 'generated-from', 'generator', 'mechanical', 'file', whole[1]))
    }
    // generated BLOCK marker
    const blk = scan.match(/<!--\s*([A-Z]+):START\s*\(generated by\s+([^\s)]+)[^]*?-->([^]*?)<!--\s*\1:END\s*-->/)
    if (blk) {
      const [, tag, gen, body] = blk
      const source = generatorSource(gen)
      nodes[`block:${path}#${tag}`] = { kind: 'generated', role: 'guard', hash: sha256(body) }
      if (source && nodes['file:' + source]) edges.push(mkEdge(`block:${path}#${tag}`, 'file:' + source, 'generated-from', 'generator', 'mechanical', 'span', gen))
    }
    // anchored fact markers -> directed, fact-granular, mechanical (FAIL LOUD if unresolved)
    for (const m of scan.matchAll(/<!--\s*fact:\s*([\w./\-]+#[\w.\-]+)\s*-->/g)) {
      const factId = 'fact:' + m[1]
      // anchored prose dep: fact-GRANULAR (fires only on that fact) but ADVISORY (a human/LLM
      // rewrites the prose) — only generated-from is mechanical/regenerable.
      if (nodes[factId]) edges.push(mkEdge('file:' + path, factId, 'derives-facts-from', 'anchored', 'advisory', 'fact', null))
      else anchorErrors.push({ file: path, anchor: m[1] })
    }
  }

  // declared edges from the steward registry (.true-up.json `seed`) — the MARKER-FREE edge path: the
  // dependency lives in config, not as an inline comment in content. `to` may be a FILE (file-granular)
  // or a FACT id `path#id` resolving to a JSON/span/symbol fact node already minted above (fact-granular,
  // early-cutoff, zero markers in the file). A `from`/`to` that doesn't resolve is FAIL-LOUD (parity with
  // <!-- fact: --> anchors) — a silently-dropped edge was the one place sidecar edges were less safe.
  const seedErrors = []
  const ensureFileNode = (ep) => {
    const id = 'file:' + ep
    if (!nodes[id] && tracked.has(ep)) {
      let c = ''
      try { c = readFileSync(resolve(ROOT, ep), 'utf8') } catch {}
      nodes[id] = { kind: extKind(ep), role: 'none', hash: sha256(c) }
    }
    return nodes[id] ? id : null
  }
  for (const e of CONFIG.seed || []) {
    const fromId = ensureFileNode(e.from)
    if (!fromId) { seedErrors.push({ from: e.from, to: e.to, reason: 'from not tracked / not found' }); continue }
    let toId
    const imported = parseImportedTarget(e.to)
    if (imported) {
      if (!validateImportAlias(imported.alias) || !EXPORT_ID_RE.test(imported.id)) { seedErrors.push({ from: e.from, to: e.to, reason: 'bad imported target syntax' }); continue }
      toId = importedNodeId(imported.alias, imported.id)
      if (!nodes[toId]) { seedErrors.push({ from: e.from, to: e.to, reason: 'imported fact not found' }); continue }
    } else if (typeof e.to === 'string' && e.to.includes('#')) {
      toId = 'fact:' + e.to
      if (!nodes[toId]) { seedErrors.push({ from: e.from, to: e.to, reason: 'fact not found (no JSON/span/symbol node)' }); continue }
    } else {
      toId = ensureFileNode(e.to)
      if (!toId) { seedErrors.push({ from: e.from, to: e.to, reason: 'file not tracked / not found' }); continue }
    }
    const kind = e.kind || 'derives-facts-from'
    const via = typeof e.via === 'string' && e.via.trim() ? e.via : null
    const importedEdge = toId.startsWith('import:')
    if (kind === 'generated-from' && !importedEdge) {
      if (!via) { seedErrors.push({ from: e.from, to: e.to, reason: 'generated-from seed requires "via"' }); continue }
      const viaRel = relative(ROOT, resolve(ROOT, via))
      if (!viaRel || viaRel.startsWith('..')) { seedErrors.push({ from: e.from, to: e.to, via, reason: 'via outside repo' }); continue }
      if (!tracked.has(viaRel)) { seedErrors.push({ from: e.from, to: e.to, via, reason: 'via not tracked / not found' }); continue }
    }
    const propagation = (!importedEdge && (kind === 'generated-from' || kind === 'alias-of')) ? 'mechanical' : 'advisory'
    edges.push(mkEdge(fromId, toId, kind, 'declared', propagation, toId.startsWith('fact:') || toId.startsWith('import:') ? 'fact' : 'file', importedEdge ? null : via))
  }

  // symlink alias edges (causal basis: symlink — the alias IS the canonical file's content)
  for (const s of symlinks) {
    if (nodes['file:' + s.to]) edges.push(mkEdge('file:' + s.from, 'file:' + s.to, 'alias-of', 'symlink', 'mechanical', 'file', null))
  }

  // tag each file/symlink node with its declared zone + intent (the graph DOCUMENTS the intention)
  const zones = loadZones()
  for (const id of Object.keys(nodes)) {
    if (!id.startsWith('file:')) continue
    const z = zoneOf(id.slice(5), zones)
    if (z) { nodes[id].zone = z.intent; nodes[id].visibility = z.visibility; if (z.audience) nodes[id].audience = z.audience }
  }

  const sortedNodes = {}
  for (const k of Object.keys(nodes).sort()) sortedNodes[k] = nodes[k]
  edges.sort((a, b) => (a.from + a.to + a.kind).localeCompare(b.from + b.to + b.kind))
  return { nodes: sortedNodes, edges, anchorErrors, spanErrors, seedErrors, importErrors }
}

