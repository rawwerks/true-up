#!/usr/bin/env node
// meta/build-contract.mjs — generate meta/contract.json (the command-surface STEWARD) from the engine's
// own `true-up capabilities`. This is how true-up dogfoods ITSELF, marker-free: the docs that restate the
// command surface (README/AGENTS/SKILL) declare a `derives-facts-from` edge to these command facts in
// .true-up.json `seed` (no inline markers), so changing a command's contract flags the docs that cite it.
//
//   node meta/build-contract.mjs           # (re)write meta/contract.json
//   node meta/build-contract.mjs --check   # exit 1 if meta/contract.json is STALE vs capabilities (CI gate)
//
// Deterministic: arrays sorted by name, no timestamps — so --check is an honest drift gate.
import { execFileSync } from 'node:child_process'
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
let cap
try {
  cap = JSON.parse(execFileSync('node', [resolve(root, 'bin/true-up'), 'capabilities'], { encoding: 'utf8' }))
} catch (e) {
  console.error(`failed to read true-up capabilities: ${e.message}`)
  process.exit(2)
}
const cleanName = (n) => (n === '(no args)' ? 'no-args' : n.split(/\s/)[0]) // first token; keep bare invocation distinct
const commands = cap.commands
  .map((c) => ({ name: cleanName(c.name), summary: c.summary, json: !!c.json, exits: c.exits || [] }))
  .sort((a, b) => a.name.localeCompare(b.name))
const agent_guidance = [
  {
    name: 'declared-seed-edge',
    summary: cap.source_of_truth?.declared_seed_edges,
  },
].sort((a, b) => a.name.localeCompare(b.name))
const out = JSON.stringify(
  { _comment: 'GENERATED by meta/build-contract.mjs from `true-up capabilities` — do not hand-edit. The steward true-up trues ITSELF against: docs derive-facts-from these command and agent-guidance facts via .true-up.json seed (marker-free).', commands, agent_guidance },
  null, 2,
) + '\n'
const target = resolve(root, 'meta/contract.json')

if (process.argv.includes('--check')) {
  let cur = ''
  try { cur = readFileSync(target, 'utf8') } catch {}
  if (cur !== out) { console.error('meta/contract.json is STALE vs `true-up capabilities` — regenerate: node meta/build-contract.mjs'); process.exit(1) }
  console.log('meta/contract.json is in sync with capabilities ✓')
} else {
  writeFileSync(target, out)
  console.log(`wrote meta/contract.json (${commands.length} commands, ${agent_guidance.length} agent guidance facts)`)
}
